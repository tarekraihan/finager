									<b>9.	PRE-SHIPMENT INSPECTION PROCESS</b><br/><br/>
									<p>
										1.	Government of many countries require pre-shipment inspections (PSI). Inspection of imports at the pint of supply is mainly 
										designed to protect the interest of the buyers in the international purchasing process A pre-shipment inspection includes: 
										physical identification of the goods in the country of supply or export to ensure that the goods are in accordance with the 
										description declared by the exporter. This is to ensure that the price is reasonably in line with current export prices from 
										the country of supply or world market prices. Verification of price provides customs with accurate data for the collection of
										import taxes and levies. The description of product, its assortment & value setting is the duly of PSI Agent.
									</p><br/>
									<p>
										2.	Pre-shipment Inspection is compulsory for these products: PSI is compulsory for all the products listed in SRO No-162-law/2000/1843/ Tariff date 08/06/2000.
									</p><br/>
									<p>
										3.	Present PSI Agent: Govt. appointed 4 PSI agents in 5 blocks.
									</p><br/>
									<p>
										4.	In the time of L/C opening, the name of the govt. appointed vistorial organization must be mentioned according to the area.
									</p><br/>
									<p>
										5.	In the PSI process, in the time of opening a L/C an importer must be carful about the description of the goods, assortment
										or HS code and prices accuracy and CRF issued by PSI agent after his inspection must be comprised with the data of L/C and
										proforma Invoice or indent.
									</p><br/>
									<p>
										6.	CRF correction tax has to be managed to send to the tax authority through direct/fax/email if there is any incompatibility found in CRF.
									</p><br/>
									<p>
										7.	Dispute Resolution: Correction of CRF- If PSI agent gives any objection, after product release, Importer can resolute the 
										dispute in three steps according to SRO No. -163-law/2000/1844/ Tariff date 08-06-2000.
									</p><br/>
									<p>
										8.	Any certification incompatible to PSI order, importer’s commercial bank guarantee and industries can discharge the invoice 
										depending on declaration.
