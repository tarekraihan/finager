									<b>1.	What is Bonded Warehouse License?</b><br/><br/>
									<p>Bonded Warehousing means the facility provided to export oriented industries for importing inputs/raw materials and packaging 
									materials without paying any duty or taxes. The National Board of Revenue (NBR) provides Bonded Warehousing benefits to a wide 
									range of industries to encourage export oriented industrialization and facilitate exports. 
									</p>
									<p>
										Bonded warehouse facility is important for the export-oriented industries, as it enhances export competitiveness. It is 
										enjoyed by both direct exporters and deemed exporters. ‘Deemed exporters’ are those industries that do not have any direct 
										export operations, and instead carry out their activities through subcontracting and receive their proceeds in foreign 
										exchange through local back-to-back letters of credit.
									</p>
									<p>
										The NBR and the Customs Bond Commissionerates effectively supervise bonded warehouse activities.
									</p><br/><br/>
									<b>2.	Categories of Bonded Warehouse License?</b><br/><br/>
									<p>
										Depending on the types of inputs/raw materials used, the purpose of their use, and the type of exports, bonded warehouses are
										basically of two categories: Special bonded warehouse and general bonded warehouse.
									</p>
									<ol class="exportImport_ol">
										<li>Special bonded warehousing is applicable for 100% export-oriented readymade garments industries, which include woven garments, knitwear and sweater manufacturing industries.</li>
										<li>General bonded warehousing applies to other 100% export-orient oriented industries. These include-</li>
										<ol type="a">
											<li>100% export-oriented ship building industry,</li>
											<li>Accessories industries for:</li>
											<ol type="i">
												<li>Deemed exports (e.g. 100% export-oriented packing/carton, label, polybag, button, hanger etc. firms), and</li>
												<li>Direct exports.</li>
											</ol>
											<li>General Bonded Warehouse for home consumption e.g. British American Tobacco. These are basically deferred payment facility for 6 months.  Duties are paid when raw materials/goods are cleared.</li>
											<li>Diplomatic & privileged persons bonded warehouse (e.g. M/s Toss Bond (Pvt) ltd., M/s H Kabir& Co. Ltd; Biman Bangladesh Airlines; Bangladesh Parjatan Corporation). Duty free articles are sold from these bonded warehouses in foreign currency to diplomats &privileged persons residing in Bangladesh.</li>
											<li>Bonded enterprises situated in the Export Processing Zones.</li>
											<li>Bonded enterprises situated in the Economic Zones.</li>
											<li>100% export-oriented deemed-to-deemed export industries.</li>
										</ol>
									</ol><br/>
