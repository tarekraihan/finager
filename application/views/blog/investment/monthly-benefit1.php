					<h3>Monthly Income Scheme and its benefits</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Fixed deposits are perhaps the most popular form of investment cum saving in India, with millions of residents choosing to secure their finances 
					by opening a fixed deposit for a particular period of time. Banks across the country offer a host of incentives to people, ensuring it is a
					win-win situation for everyone involved. Given the Indian mind-set to save every last rupee, a fixed deposit is a must in every household, 
					offering peace of mind to all concerned.
				</p>
				<p>
					Today, banks offer different options when it comes to fixed deposits, with multiple payment plans and bonuses available. One of the most popular 
					fixed deposits relates to the monthly income schemes, which, true to their name, offer a monthly income to account holders.
				</p>
				<br/>
				<h4><b>Benefits of monthly income scheme</b></h4>
				<p>
					<b>Regular income</b> – Under a monthly income plan an individual is entitled to earn regular monthly income, supplementing his/her original income
					source. With rising expenses, an additional income source can be a necessity today, helping one meet financial requirements.
				</p><br/>
				<p>
					<b>Tenure</b> – Individuals can choose a tenure which suits their needs, with most banks offering tenures ranging from a year to 10 years.
				</p><br/>
				<p>
					<b>Direct credit</b> – The monthly income is directly credited to the savings or current account of an individual, ensuring that he/she doesn’t have
					to face any hassles of going to the bank and withdrawing it, saving time and energy.
				</p><br/>
				<p>
					<b>High interest</b> – Fixed deposits offer a decent interest, minus the risk involved with other such lucrative investments. This high interest 
					helps your initial money grow over time, helping one beat inflation.
				</p><br/>
				<p>
					<b>Additional interest for senior citizens</b> – Senior citizens could benefit tremendously from a monthly income plan, given the fact that most of 
					them do not have a regular income source post retirement. This income can help them lead a normal lifestyle, without having to worry about 
					finances.
				</p><br/>
				<p>
					<b>Nomination facility</b> – Most banks provide a nomination facility wherein account holders can nominate an individual to receive the amount if something happens to them.
				</p><br/>
				<p>
					<b>Liquidity</b> – Most people are of the opinion that fixed deposits do not offer any liquidity in terms of finances, but a monthly income plan ensures
					that you have sufficient funds at your disposal, making it highly liquid. This ensures that any financial emergencies can be met without having 
					to break a fixed deposit.
				</p><br/>
				<p>
					<b>Overdraft/loan</b> – Most banks offer an overdraft facility on the fixed deposit, thereby helping an account holder meet any additional financial requirements.
				</p><br/>
				<p>
					Opening a fixed deposit with a monthly income plan can be an extremely smart investment choice, offering flexibility and ease of use to individuals,
					apart from financial comfort to meet any contingency.
				</p>
