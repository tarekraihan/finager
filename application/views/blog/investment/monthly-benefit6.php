					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					The rate of interest payable in Monthly Income Scheme (MIS) accounts in post offices is 8 per cent per annum and Bonus at the rate of 5 per cent
					is also payable on the deposits made under this scheme on or after 8th December, 2007 upon maturity of the deposit. The interest rates on small 
					savings schemes are regulated/reviewed as per recommendations of Committee on Administered Interest Rates and other related issues chaired by 
					Dr. Y.V.Reddy, the then Deputy Governor, Reserve Bank of India(RBI) , according to which, being administered interest rates, they are benchmarked 
					to average annual yield on Government Securities of comparable maturity in the secondary market, with a suitable spread subject to a maximum of
					50 basis point over the benchmark yield, depending upon the maturity and liquidity of the instruments.
				</p>
				<p>
					Bonus at the rate of 5 percent of the amount deposited in MIS accounts is payable in the accounts opened on or after 8th December, 2007 upon
					maturity of the deposit. The Central and State Governments take various measures from time to time to promote and popularize small saving schemes 
					through print and electronic media as well as holding seminars, meetings and providing training to various agencies involved in mobilizing 
					deposits under the schemes. As part of this ongoing exercise, Government has taken following steps to make the small savings schemes more 
					attractive and investor friendly:-
				</p>
				<p>
					The benefit of Section 80C of the Income Tax Act, 1961 has been extended to the investments made under 5-Year Post Office Time Deposits Account and Senior Citizens Savings Scheme, with effect from 01.04.2007.
				</p>
				<p>
					With effect from 1.8.2007, the maximum deposit ceilings of Rs.3.00 lakh and Rs.6.00 lakh under the Post Office Monthly Income Account (POMIA)
					Scheme has been raised to Rs.4.50 lakh and Rs.9.00 lakh in respect of single and joint accounts respectively.
				</p>
				<p>
					The penalty on pre-mature withdrawal of deposits under the Post Office Monthly Income Account (POMIA) scheme has been rationalized from 
					3.5% to 2% on withdrawal on or before expiry of three years and 1% on withdrawal after expiry of three years.
				</p>
				<p>
					All categories of pensioners have been allowed to open and maintain ‘Pension Account’ under Post Office Savings Account Rules, with effect from 11th July, 2007.
				</p>
				<p>
					The restriction on opening of more than one account during a calendar month under the Senior Citizens Savings Scheme has been removed with effect from 24th May, 2007.
				</p>
				<p>
					Opening of “Zero deposit/Zero Balance” accounts for workers employed under NREG Act, under Post Office Savings Account Rules, with effect from 26th August 2008.
				</p>
				<p>
					Opening of “Zero deposit/Zero Balance” accounts for Old Age Pensioner Account under Indira Gandhi Old Age Pension Scheme, Widows Pensioner Account
					under Indira Gandhi National Widow Pension Scheme and Disabled Pensioner Account under Indira Gandhi National Disabled Pension Scheme with effect 
					from 13th October 2009.
				</p>
				<p>
					National Savings Institute, a subordinate organization under the Department of Economic Affairs (Budget Division) also maintains its web site i.e 
					nsiindia.gov.in in collaboration with National Informatics Centre to facilitate interface with the public through wider dissemination of 
					information on small savings and on-line registration and settlement of investor’s grievances.
