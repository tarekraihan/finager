					investment options to the public. And at the same time, the finances are used to mobilize resources for development. Post office monthly income 
					scheme (POMIS) is one such endeavor of the Government of India. This Post Office scheme is more suited for those investors who are looking to 
					invest a fixed sum of money and want guaranteed returns every month. Under the POMIS scheme, for an investment of 4.5 lakh, one can earn INR 3,000 
					per month as interest. Post office schemes have not really attracted investors in a big way because, they are considered old-fashioned schemes in
					this competitive economic era. But POMIS is a very good plan for those who are want guaranteed monthly payment for their livelihood. The rate of 
					interest is 8.4% per annum. The maturity period is 5 years, meaning investors will get back every single penny that they had invested, at the end 
					of the term. The minimum amount that an investor can invest is Rs 1,500 and maximum would be Rs 4.5 lakh. Now, if the investor wishes to withdraw
					the amount before maturity period - he will get nothing if withdrawal is done within a year, else he will get back his deposit with a deduction 
					of 2% (as a penalty) if withdrawal is done between 1 and 3 years. A deduction of 1% is applicable if the money is withdrawn after three years. 
					Another thing investors must keep in his mind is that if he does not withdraw his monthly investment, he will not get any interest on that 
					amount (that is generated as monthly interest). Also one should know that this POMIS does not come under Section 80c, so there is no tax exemption
					on the amount invested. Interest income is taxable, but there is no TDS cut in this scheme. The minimum deposit amount is INR 1,500 and maximum 
					limit is INR 4.5 lakh for a single account and INR 9 lakh for a joint account. POMIS accounts are transferable from one post office to other, 
					free of cost. The maturity amount realized at the end of the term can be reinvested in POMIS. And the most important thing is that, there is no
					fear and risk of losing the money as Post office is one of the most trustworthy and reliable departments in India. They are one of the oldest 
					institutions providing such investment services. So for what have you been waiting for? Need monthly return on your investment? Then get started 
					with a POMIS right away. 
