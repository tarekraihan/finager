					<h3>Reminder for landlords to protect deposits</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					More than £1.75 million worth of deposits have been securely placed in the States’ approved tenants deposit scheme which came into force on 2 November 2015.
				</p>
				<p>
					Landlords and letting agents are being reminded to protect their tenants’ deposits in the new mydeposits Jersey tenancy deposit scheme.
				</p>
				<p>
					Under the new law, all landlords and agents that take a deposit from their tenant on a new, renewed or varied tenancy agreement must transfer the money to 
					mydeposits within 30 working days of receiving it.
				</p>
				<p>
					Landlords and agents cannot avoid the Law by taking rent in advance instead of a deposit, and those who fail to comply with the Law will be investigated and could be fined up to £2,000.
				</p>
				<p>
					Minister for Housing, Deputy Anne Pryke said:
				</p>
				<p>
					“The tenancy deposit scheme is in place to safeguard deposits for the length of a tenancy. It’s important that both landlords and tenants are aware of these
					important legal requirements.
				</p>
				<p>
					“Landlords need to act responsibly when dealing with tenants’ money. It doesn’t matter what description is given to this money, if it could be returned to the tenant, 
					it is a deposit and must be protected with mydeposits Jersey.”
				</p>
				<p>
					The Residential Tenancy Law makes it clear that any money that a tenant pays a landlord as security for damages or to insure against the tenant not meeting their
					obligations is a deposit and must be paid into the tenancy deposit scheme. Rent in advance is legal but it does put landlords at risk as they would be unable to 
					make an immediate claim for any damages caused by the tenants. They may also struggle to recover rental arrears without going through the Petty Debts Court.
				</p>
				<p>
					Eddie Hooker, mydeposits Jersey chief executive, adds:
				</p>
				<p>
					"I’ve seen first-hand how deposit protection has helped to raise renting standards, it reduces unfairly withheld deposits and improves landlord - tenant relationships.
				</p>
				<p>
					“Tenants who are unsure if their deposit has been correctly protected should speak to their landlord or letting agent directly. The mydeposits Jersey website has
					a deposit checker for tenants to check that if their deposit has been protected.”
				</p>
