					<h3>What is different about the tenancy deposit schemes?</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					There are several answers to this question.
				</p>
				<p>
					The first is that there are going, from 1 April 2016, to be two new custodial tenancy deposit schemes. One from TDS and one from My Deposits.
				</p>
				<p>
					Another is, from 1 April 2016, very little.
				</p><br/>
				<h4><b>In the beginning</b></h4>
				<p>
					When the tenancy deposit schemes first launched in 2007 it was fairly easy.
				</p><br/>
				<p>
					<b>If you were an agent</b>, you used TDS. TDS is owned by ARLA and RICS and was originally set up (before the compulsory schemes came into force) to provide a
					voluntarily tenancy deposit scheme for agents.
				</p>
				<br/>
				<p>
					<b>If you were a landlord</b>, you used My Deposits. My Deposits is owned jointly by the National Landlords Association and Hamilton Fraser and was set up specifically 
					so there would be an insurance based tenancy deposit scheme developed with landlords in mind.
				</p>
				<p>
					<b>If you wanted a free scheme</b> you used the DPS. The DPS ran the only custodial scheme – a custodial scheme being free to the user but the money has to be paid over to the scheme administrators.
				</p>
				<br/>
				<p>
					It wasn’t absolute but that was the general idea.
				</p>
				<br/>
				<h4><b>Extra schemes</b></h4>
				<p>
					After a while, things started to change. First TDS joined up with the Residential Landlords Association and created a TDS scheme for landlords called Deposit Guard.
					This had similarities with the My Deposit scheme save that it was a bit cheaper and there was more inbuilt support. 
				</p>
				<p>
					Then DPS were given leave to set up an insurance based scheme (or maybe it was the other way around, I can’t remember the exact dates things happened).
				</p>
				<p>
					My Deposits also became far more focused on agents, particularly after the company became associated with agent organisation UKALA.
				</p>
				<p>
					So the main difference between the schemes seemed to be that the DPS had a custodial scheme and the others did not.
				</p>
				<p>
					Now that too is to change. Announcements have been made that both TDS  and My Deposits have been given leave to set up their own custodial schemes from April 2016.
				</p>
				<p>
					But is this a good or a bad thing?
				</p>
				<br/>
				<h4><b>The benefit of competition</b></h4>
				<p>
					I don’t think it’s a bad thing that there should be a certain amount of competition. For example the big complaint about the DPS has always been how long it
					takes to refund tenants money at the end of the tenancy.
				</p>
				<p>
					However new DPS MD Julian Foster has now said that deposits where there is no dispute take an average of two days to be returned.
				</p>
				<p>
					Is this improvement down to the prospect of imminent competition from the other schemes? I don’t know but it may be at least a contributing factor.
				</p>
				<br/>
				<h4><b>Who will pay?</b></h4>
				<p>
					Custodial schemes are free to use – or at least the DPS scheme is, and I am assuming that the other two new custodial schemes will be.
				</p>
				<p>
					When the schemes were set up, the general idea was that their running costs would be paid for by the interest on the deposit money held by the scheme. 
					However since then we have had a massive drop in interest rates – how can a custodial scheme still be financially viable?
				</p>
				<p>
					The interview with Julian Foster in Property Eye revealed that the DPS, faced with financial difficulties, reached a new agreement with the government a few years
					ago where, in place of the previous government ‘bail out’ guarantee (which could have cost the nation a fortune), it was made a payment of £12.7m.
				</p>
				<p>
					Are the other two new tenancy deposit schemes getting similar payments (and if so why should the rest of us fund tenancy deposit schemes when there is a self-funding 
					model available) or are they being cross funded by the insurance based schemes?
				</p>
				<p>
					I’d quite like to know about that.
				</p>
				<p>
					And if they are being cross funded:
				</p>
				<p>
					Is it fair for paying customers to fund the free scheme and What is going to happen if more landlords and agents switch to (free) custodial schemes?

				</p>
				<br/>
				<h4><b>But what is the difference?</b></h4>
				<p>
					I read an interesting book recently called Different: Escaping the Competitive Herd . It is written by Youngme Moon, a professor of marketing at the
					Harvard Business School in America.
				</p>
				<p>
					Among the things she considers in the book is the reason why all businesses and products in the same sector tend after a while to look the same. The result being 
					that customer loyalty diminishes and people tend to choose on the basis of who is offering the best deal at the time.
				</p>
				<p>
					Moon points out that the harder companies compete, the less differentiated they become – because they all compete for excellence at the same things.
					All you get in the end therefore is a ‘proliferation of sameness’.
				</p>
				<p>
					Which just confuses the consumer.
				</p>
				<p>
					I don’t think we are quite there yet with tenancy deposit schemes, but we I think we are getting to the stage where a novice landlord is going to look 
					through the list and wonder what the difference is.
				</p>
				<p>
					I hope that when we have more information about the new schemes there really is some difference between them, and that there are options which are obviously
					more suitable for different types of landlord and agent.
				</p>
