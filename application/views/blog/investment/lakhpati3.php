					<h3>How to Deposit Cash at an Online-Only Bank</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Online-only banks can let you deposit checks from your phone, but the same can’t be said for cash. In fact, it’s a transaction they haven’t completely figured out.
				</p>
				<p>
					“Digitizing cash is the last frontier to conquer for widespread online-only bank adoptions,” says Michael Moeser, director of payments at Javelin Strategy & Research 
					in Pleasanton, California. Approximately 1% of consumers use an Internet-only bank as their primary financial institution, according to Javelin.
				</p>
				<p>
					But these banks are growing in popularity. From 2004 to 2014, branchless banks’ share of transaction deposits in the U.S. grew 400%, going from 0.5% of the total to
					2.6%, according to Novantas, a banking analytics and advisory firm in New York.
				</p>
				<p>
					Here are some ways to add cash to an online-only bank account:
				</p>
				<br/>
				<h4><b>1. Use deposit-accepting ATMs</b></h4>
				<p>
					Online-only banks tend to participate in nationwide ATM networks such as STARsf, MoneyPass and Allpoint. Not all of them let you deposit cash, so check with your bank
					to see if its ATM network does. Capital One 360, for example, lets you use only select ATMs.
				</p>
				<p>
					Some banks, such as First Internet Bank of Indiana, may put a mandatory hold on your money for several days if you deposit cash at an ATM it doesn’t own or operate.
				</p>
				<br/>
				<h4><b>2. Use a reloadable prepaid debit card.</b></h4>
				<p>
					To make frequent cash deposits, consider buying a reloadable prepaid debit card. But keep in mind that some come with fees, such as monthly membership fees.
				</p>
				<p>
					You can generally add cash at certain stores. American Express Bluebird and GoBank cards, for example, let you add cash at most Wal-Mart registers for no additional
					fee. There may be daily and monthly deposit limits, as well as a wait time before the money becomes available.
				</p>
				<p>
					Once the card is loaded, you can send the money to a linked account at your online-only bank.
				</p>
				<br/>
				<h4><b>3. Use a money order.</b></h4>
				<p>
					Another option is buying a money order, which is like a personal check for a sum that you pay in advance. You can then deposit it using your smartphone and a mobile 
					banking app. A $1,000 money order costs less than $2 at a U.S. Post Office. Western Union and Moneygram also offer them for fees that vary by location.
				</p>
				<br/>
				<h4><b>4. Deposit into another account and transfer.</b></h4>
				<p>
					If you or a friend has an account at a bank with a nearby branch or ATM, you can deposit the money there, and then transfer it to your account online or by check. 
					Note that whoever sends the money may get charged a fee. Banks can charge for bank-to-bank transfers, also called ACH transfers, but online banks generally don’t 
					charge a fee for those that are incoming.
				</p>
				<p>
					Although adding cash to an online-only bank account takes more effort than a mobile check deposit, there are various ways to do it. 
					These options can save you time and energy, especially if a wad of bills comes your way.
				</p>
