				<div class="details_heading">
					<h3>Four Reasons Why You Didn't Get the Best Fixed Deposit Rate From Your Bank</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Fixed Deposits lately come with advertisements of super high, attractive interest rates but many people eagerly sign up only to find they didn't get the
					rate thought they would. Here are four probable reasons why that happened.
				</p>
				<p>
					Fixed deposits are awesome things: they're a little like saving accounts with much higher interest rates. This not only encourages you to save more, it means
					allowing you to hit your saving goals faster with a little 'help' along the way.
				</p>
				<p>
					But fixed deposits (FDs) aren't made equal and even though a bank may blare a particularly scrumptious interest rate on their flyer; it may not be the super rate that 
					you get when you eagerly splash your cash on the counter. That said, it doesn't mean your FD is a scam or that the banks are evilly trying to con you with fake
					deals - it just means you didn't read the fine print.
				</p>
				<p>
					Here are reasons why you probably got a lower rate than the one advertised.
				</p><br/>
				<h4><b>You Didn't Deposit Enough</b></h4>
				<p>
					Ah, the elusive measure of 'enough'. Only for banks, this measure isn't so elusive. Depending on the FD product you signed up for; the 'enough' mark could be anywhere 
					from RM5,000 to RM10,000. This means that in order -to qualify for their they-must-be-loco interest rates of 4% p.a. and up; you'll have to deposit more than the set 
					threshold. Depending on the bank or the mechanics of the FD promotion; the usual minimum amount you need to deposit (that we've seen!) is RM10,000. This amount will
					usually give you really good returns.
				</p>
				<p>
					Why is this? The banks need your deposit for investments and profit growing - it's less admin cost to manage one client depositing RM10,000 than 10 clients depositing
					RM1,000. This encourages them to encourage you to give them less work and earn more interest for yourself
				</p>
				<br/>
				<h4><b>You Didn't Select the Right Tenure</b></h4>
				<p>
					Beyond the minimum amount needed to secure the most attractive interest rate for your deposit; you also need to select the right tenure. All FDs have tenures within 
					which you cannot withdraw the money or you will lose any interest accrued. This is how an FD differs from a savings account.
				</p>
				<p>
					Tenures range from one month to 36 months and the longer you select; the higher your chances of scoring that delectable rate on the top tier of FDs.
				</p>
				<p>
					Why is this? The longer you deposit your cash with the bank; the longer they are able to keep it in their various investments, earning more profits for themselves and
					you. This also reduces the admin costs associated with people constantly withdrawing their funds.
				</p>
				<br/>
				<h4><b>You Are Not a Previous Customer of That Bank</b></h4>
				<p>
					Banks often reward loyal customers (that is, those with loans or other products with the bank) with preferential FD rates. Often offered to preferential banking 
					customers, these attractive rates are exclusively open to these customers.
				</p>
				<p>
					Perhaps you've accidentally come across a leaflet intended for the loyal returning customer - if you don't read the fine print; you may not realise this.
				</p>
				<p>
					Why is this? Just like retailers offer exclusive discounts and freebies to their loyalty card members or frequent shoppers; banks opt to reward loyalty in this
					way - by encouraging the already-customer to take out more financial products from them instead of with a competitor.
				</p>
				<br/>
				<h4><b>You Did Not Use Fresh Funds</b></h4>
				<p>
					Fresh funds don't necessarily refer to fund just in from your paycheck - but funds not already deposited with the bank. This is linked to the point above about being a previous customer.
				</p>
				<p>
					Say you are a previous customer with a checking account at Bank A. Suddenly, you notice a promotion offering loyal bank customers a 4.25% FD rate if you deposit
					RM5,000! Happily; you remove your money from your Bank A checking account to chuck into the more lucrative FD. Hold your horses! You won't be earning top interest 
					this way! Many loyal customer promos (if not all) will require you to bring 'fresh funds' into the bank. This means, funds from either under your pillow or from 
					another bank!
				</p>
				<p>
					Why is This? Why competition of course! Like a jealous lover; your bank may be wondering where else you are parking your 'assets'. By enticing you with hefty returns; 
					they hope you bring all your stray funds into one place. This includes those stray funds hiding under the floorboards!
				</p>
				<p>
					What is a depositor to do? Well, you can always ensure you get the best rates by only depositing amounts of RM10,000 for a minimum of 12 months at a bank you already
					frequent with money you previously kept elsewhere. But how feasible is that, really? Instead, how about just reading the terms and conditions to know exactly what
					you will be getting in your FD and if it's worth the investment when compared to other FDs in the market. Compare based on the amount you have and tenure you are most 
					comfortable with. You may or may not get the highest return but you will certainly be getting the most for your money.
				</p>
				<p>
					Don't want to do the legwork? No sweat, we did it for you and we constantly will - whilst offering you exclusive FD promotions and freebies when you apply with us.
				</p>
