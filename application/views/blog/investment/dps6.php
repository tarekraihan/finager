					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<h4><b>Deposits</b></h4>
				<p>
					1.	To hold the funds in their own bank accounts and pay an Insurance Premium to cover the cost of potential arbitration in the event of a dispute at the end of 
					the tenancy and to cover the cost of running the scheme
				</p>
				<p>
					2.	To pay all of the money into the Custodial Scheme where the interest made on the funds held would pay for the potential arbitration in the event of a dispute 
					at the end of the tenancy and to cover the cost of running the scheme
				</p>
				<p>
					Having made that decision the landlord may have a further choice to make if he decides to hold the funds and use an Insurance based scheme because there were, until now, two options.
				</p><br/>
				<p>
					The DPS is the only Custodial scheme, the other two offer the Insured option.
					From 1st April there will be two further Insurance options because Government have issued two new Contracts.
					<ol style="margin-left:15px">
						<li><b>DEPOSIT PROTECTION SERVICE</b> (DPS) www.depositprotection.com</li>
						<li><b>MYDEPOSITS</b> www.mydeposits.co.uk</li>
						<li><b>TENANCY DEPOSIT SCHEME (TDS)</b>  www.tds.gb.com</li>
					</ol>
				</p>
				<br/>
				<p>
					Any <b>Deposit Protection Scheme</b> must hold a Government Contract in order to operate and the three schemes that have held a Contract until now are
					<ol style="margin-left:15px">
						<li><b>CAPITA TDP</b> (CapitaTDP) www,capita-tdp.co.uk</li>
						<li><b>DEPOSIT PROTECTION SERVICE INSURED SCHEME</b> (TDP INSURED) www.depositprotection.com/insured-deposit-info</li>
					</ol>
				</p>
				<p>
					Information about legislation related to Tenancy Deposit Protection and the various schemes that are Government approved can be found here https://www.gov.uk/tenancy-deposit-protection/overview.
					It is very important that landlords do not put deposits into any scheme that is not authorised by Government, there have been bogus web sites over the last few years and landlords have been caught out.
				</p>
				<br/>
				<p>
					1.	The need to protect tenants deposits within the prescribed time frame. The changes in the Localism Act mean that a landlord has only 30 days from the time that
					the deposit is paid to protect it, and to give the tenant and any third party who may have paid the deposit, the Deposit Protection Certificate and the Prescribed 
					Information for Tenants provided by the scheme.  Failure to meet these legal requirements could cost the landlord up to 400% of the deposit.
				</p>
				<p>
					2.	The need to meet all the rules of the chosen scheme, this is part of the legislation and must not be ignored. These vary from scheme to scheme and can change
					within a scheme from time to time.  Landlords MUST check the website of their chosen scheme on a regular basis to make certain that they meet with any new requirement 
					because failure to do so may mean that the tenants deposit has not been protected as it should have been throughout the whole tenancy.
				</p>
				<br/>
				<p>
					An issue that has come to light recently is that one of the schemes changed their rules in January 2012.  The change meant that a deposit needed to be re-protected 
					when a fixed term ended and the tenancy rolled over into a Statutory Periodic Tenancy.  If the landlord did not re-protect the deposit at that point the deposit was
					no longer protected and the landlord will have broken the law.  The penalties for this are severe because a court may award the tenant up to 400% of the deposit as
					compensation.  This can hang over a landlords head for up to 6 years after the tenancy has ended. Other schemes may ask the landlords to update the record to show 
					that the tenancy has continued beyond the fixed term and no new contract has been put into place, these schemes may not issue a new certificate and will continue to 
					protect the deposit until it is unprotected, their request for an update is for administrative purposes only.  A new contract (AST) always means a new protection with 
					an Insurance based scheme.
				</p>
				<p>
					Tenancy deposit protection has become an administrative burden for landlords and many are choosing not to take a deposit, this is of course a commercial decision and
					we must all make our choice.  Please be warned that any money taken to cover losses or damages is, in law, a deposit regardless of what the landlord chooses to call it,
					and must be protected. There is a case pending where the landlord took rent in advance and did not protect it as a deposit and we are waiting to hear the ruling on 
					whether rent taken before it is due for payment is, in fact, a deposit or not. I will stick my neck out and say that, in my opinion, rent taken before it is due is in
					fact a deposit because it is taken to protect the landlord from non payment of rent.  Rent taken in advance where the next payment is not due until the period covered 
					by the advance rent has, or is about to expire, is rent in advance not a deposit and does not need to be protected.  There is a danger in extending the rental period 
					in this way but that is the subject of another article related to Possession and I will not complicate matters by discussing it now. It will be interesting to see how 
					the court rules on the case that is pending.
				</p>
