				<div class="details_heading">
					<h3>Nothing special about Post Office Monthly Income Scheme</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					There are a number of us who seek regular income from their investments. This is especially true for retirees. Post offices offer a few schemes 
					that cater to such needs of the investors. In one of the earlier posts, we had discussed Senior Citizens Savings Scheme (SCSS) that offers 
					attractive returns and tax benefits to senior citizens.
				</p>
				<p>
					Post offices offer other products too where you do not need to be a senior citizen to invest. Post Office Monthly Income Scheme (POMIS) is one
					such scheme. POMIS offers guaranteed monthly returns. Needless to say, only post offices offer POMIS. Let’s find out more about the scheme and 
					see how it fares against regular bank fixed deposits. Finally, we will assess if POMIS deserves a place in your investment portfolio.
				</p>
				<br/>
				<h4><b>Post Office Monthly Income Scheme (POMIS)</b></h4>
				<p>
					Under this scheme, investors are paid interest monthly. For FY2015-2016, the applicable interest rate is 8.4% p.a. The Government of India 
					notifies the interest rate for the scheme at the beginning of the financial year. Please note the interest rate remains constant for the tenor
					of the deposit (irrespective of any changes in the interest rate by the Government in the subsequent years).
				</p>
				<p>
					Interest payout is made each month starting one month from the date of investment (and not from the start of month). Since there is regular
					interest payout, there is no benefit of compounding.
				</p>
				<p>
					Since the POMIS is backed by Government of India, there is no credit risk.
				</p>
				<br/>
				<h4><b>Eligibility</b></h4>
				<p>
					Such an account can be opened by a resident individual. Non-resident Indians (NRIs) and Hindu Undivided family (HUF) cannot invest in such schemes.
					Account can be opened in the name of a minor.
				</p>
				<br/>
				<h4><b>Maximum Investment Limit</b></h4>
				<p>
					An investor can invest a maximum of Rs 4.5 lacs in a single account or Rs 9 lacs in a joint account (maximum 3 holders).  An account can also be 
					opened in the name of a minor. An individual can open any number of such accounts. However, his/her total share in all the accounts combined 
					(individual or joint) cannot exceed Rs 4.5 lacs. For calculation of share in a joint account, each individual in a joint account is supposed to 
					have equal share.
				</p>
				<br/>
				<h4><b>What happens if you deposit more?</b></h4>
				<p>
					You may not get any interest on the excess amount. Though nothing is mentioned about treatment of excess deposit in Post Office (Monthly Income 
					Account) Rules, 1987, the rules are quite clear about the maximum investment limit.
				</p>
				<p>
					There is a maximum investment limit of Rs 4.5 lacs per person. It is quite possible that your excess deposits may go unnoticed. However, be
					prepared that you may not get any interest on the excess amount.
				</p>
				<br/>
				<h4><b>Premature Withdrawal</b></h4>
				<p>
					Maturity period is 5 years. The amount can be prematurely encashed /withdrawn after 1 year. Withdrawal after first year till the end of third year
					will attract a penalty of 2% of the deposit amount. Withdrawal after 3 years and before maturity will attract a penalty of 1% of deposit amount.
				</p>
				<br/>
				<h4><b>Tax Benefits</b></h4>
				<p>
					There are no associated tax benefits for investments in the scheme. There is no tax deduction for investing in the scheme and interest earned is 
					taxable. There is no tax deduction on source (TDS). You will have to pay tax as per your income tax slab while filing Income Tax returns.<br/><br/>
					<img src="../images/blog-table.jpg" alt="Table">
				</p>
				<br/>
				<h4><b>PersonalFinancePlan Take</b></h4>
				<p>
					POMIS appears to be good option for those investors looking for regular income. However, a cap on maximum investment limit (Rs 4.5 lacs per person)
					and lack of any favourable tax treatment takes away the entire sheen. Additionally, the penalty for premature withdrawal is quite high. A regular 
					bank fixed deposit with monthly payout is just as good. You can find FDs with competing or even better interest rates. Bank FDs also do not have 
					a maximum investment limit.	
				</p>
				<p>
					Investors can opt for a tax savings FD (with lock-in of 5 years) too with a monthly interest payout.  This way, they can get tax benefit under
					Section 80C too. However, the maximum tax benefit is limited to Rs 1.5 lacs per financial year.
				</p>
				<p>
					Senior Citizens get even higher interest rates for FDs. They also have an option of investing in Senior Citizens Savings Scheme (SCSS) at a much
					higher interest rate (9.3% per annum for FY2016, quarterly payout). The maximum investment limit is Rs 15 lacs and there is tax deduction under
					Section 80C too.
				</p>
				
