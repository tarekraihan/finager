					<h3>How and Why Do Interest Rates on Fixed Deposits Fluctuate</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					For a number of Indians, a Fixed Deposit is the preferred option when it comes to investing. Why? Because Fixed Deposits offer attractive rates of interest, 
					generally about 9%, and provide assured returns.
				</p>
				<p>
					The downside that comes with FD investments is taxation. The interest earned from Fixed Deposits is taxable under Section 80C of the Income Tax Act. Even with the
					taxation law, FDs are an attractive form of investment amongst the Indian masses. However, Fixed Deposit interest rates in 2016 are decreasing and curtailing the 
					perks of receiving adequate returns. Let’s evaluate why.
				</p>
				<br/>
				<h4><b>Are Fixed Deposit Rates Going to Increase?</b></h4>
				<p>
					If you invest in an FD today, you could get a rate of interest between 8.5% and 9.25%. Some banks and NBFCs will offer a higher rate of interest for a five-year 
					term deposit than for a three-year term. NBFCs like Bajaj Finserv offer returns to their investors up to four times a year and also provide access to an online 
					account to keep them up to date about their investment.
				</p>
				<p>
					However, on April 15th, 2016, the RBI cut the base repo rate by 25 basis points. This brought down the rate of interest on FDs to 6.5%. According to Fixed Deposit
					interest rates forecast, this rate of 6.50% will most likely be prevalent up to the end of this quarter. In the long-term, the rate of interest is projected to be
					around 4.75% by the year 2020. This only shows that rates of interest for FDs will decrease, thereby making it difficult for FD investors to earn good returns.
				</p><br/>
				<h4><b>What Influences Rate Changes?</b></h4>
				<p>
					According to financial experts, interest rates are influenced by certain macroeconomic conditions like inflation. Due to these conditions and the need to regulate the
					credit balance in the country, the RBI creates new policies. They generally do this by increasing repo rates, thereby gradually boosting Fixed Deposit rates. Finally, 
					as a result of this, the cash reserve ratio brings in more money into the system.
				</p>
				<p>
					Here are some factors that influence banks and NBFCs to either increase or decrease the Fixed Deposit rates.
				</p>
				<p><b>•	Demand and Supply for Credit</b></p>
				<p>
					The demand of supply greatly influences Fixed Deposit rates in India. If the demand for credit is low, then banks will generally decrease FD rates. On the other hand,
					if the demand for credit is high, the Fixed Deposits rates will be increased.
				</p>
				<p><b>•	Economic Scenario in the Country</b></p>
				<p>
					If there is an adequate flow of money in the country, banks and NBFCs won’t have to depend on retail Fixed Deposits, unlike when liquidity is low and they have to 
					turn to their own deposits. This leads to an increase in the FD rates.
				</p>
				<p><b>•	Rate of Inflation</b></p>
				<p>
					When you deposit money in a Fixed Deposit, it’s wise to monitor the inflation rates in the country, which consequently affect FD rates. Banks and NBFCs should give 
					their depositors positive returns but during high inflation, many investors get negative returns. In spite of this, many financial institutions don’t raise their
					rates as it affects their net income.
				</p>
				<p><b>•	Market Standings</b></p>
				<p>
					If the market doesn’t have enough liquidity, this will bring about an FD rate cut. If the market is lending to banks at a lower rate of interest, then it
					affects the rates on all retail deposits.
				</p>
				<p>
					Choose your Fixed Deposit tenure wisely as it will impact your returns. At Bajaj Finserv, you can enjoy flexible tenures on deposit investments.
				</p>
