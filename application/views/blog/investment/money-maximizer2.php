				<p>
					The money multiplier does not expect you to formally open a FD every time you have surplus and offers you a regular Fixed Deposit Interest. Hence, it is very good.
					However, the catch here is, when you withdraw money, the penal interest will be charged since your FD did not complete the duration. 
				</p>
				<p>
					So, if you intend on actually saving the money for long term, you should consider opening a regular FD or RD and only keep liquid cash you need for your day to day expenses in your account. 
				</p>
				<p>
					India is the Land of Miracles and Indians are a special breed. We want “Safety and Security” in everything we do. In fact, up until a couple of decades before, anyone 
					working in a Private Sector Firm was considered Crazy to risk their careers because only a “Government Job” was considered Safe. When it comes to Investments, our 
					forefathers were even more cautious. Anything that did not have the “Backing” of the Government was not even remotely considered.
				</p>
				<p>
					Even with all the recent developments in Technology, Education and the world of Finance, we Indians are still very reluctant to invest in the Stock Market. If we try 
					to think about the reason for this, our Mentality and Conservative Mindset occupy the top slots. Anyways, the purpose of this book is not to analyze why our mindset 
					is like that…
				</p>
				<p>
					In order to cater to the Conservative Investor Population, there is a whole Myriad of Investment Products available in India that is not only fully 100% safe but also 
					gives you good returns. Most of our friends and family members would be interested in safe investment options but aren’t fully aware of what they are. The purpose of
					this book is to help you understand in great detail about these “Safe Investment Havens”.
				</p>
				<p>
					Money Multiplier feature gives you the liquidity of a Savings Account coupled with high earnings of a Fixed Deposit. This is achieved by creating a Fixed Deposit
					linked to your Savings Account providing you the following unique facilities.
				</p>
				<br/>
				<h4><b>Maximum Returns</b></h4>
				<p>
					Your money is never idle. Creation of a linked FD ensures higher rate of interest on your Savings Bank Deposits. You can issue instructions through any channel such
					as the ICICI Bank Branch, ICICI Bank Phone Banking and ICICI Bank's Internet Banking for creation of Fixed Deposit(s) from the surplus funds in your Savings Bank 
					Account (subject to a minimum of Rs. 10,000). The Fixed Deposits will be created in multiples of Rs. 5,000 for a tenure of one year or more as instructed by you.
				</p>
				<br/>
				<h4><b>Maximum Liquidity</b></h4>
				<p>
					You can withdraw the funds from your savings account through any channel such as the ICICI Bank ATM, ICICI Bank Internet Banking or by issuing a cheque. etc. All 
					linked Fixed Deposits will be enabled for automatic Reverse Sweep in multiples of Rs. 5000 on a Last-In-First-Out (LIFO) basis when the balance in the Savings Account 
					falls below Rs.10,000. The amount reverse swept will earn interest rates at the applicable rate for the period that the deposit was held with the Bank. The remaining
					amount will continue to earn higher interest at the original rate applicable to the fixed deposit.
				</p>
				<br/>
				<h4><b>Auto-Renewal</b></h4>
				<p>
					Under this facility, when your deposits fall due, the bank will automatically renew the principal and accrued interest for the same tenure as the original deposit.
				</p>
