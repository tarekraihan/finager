					Table 1 shows the rates that were existing and also the revised rates for fixed deposits that are less than Rs. 1 crore in value and those that range from Rs. 1 crore
					to Rs. 10 crore. Table 2 shows the existing and revised rates for aggregate bulk deposits for Rs.10 crore and above. (Footnotes appended for additional information).
				</p>
				<p>
					Aggregate bulk deposit above Rs.10 crore with effect from 07.07.2014:-
					Prior approval of Treasury Central Office must be taken before accepting deposit of Rs.25 crore and above.
				</p>
				<p>
					These interest rates are not applicable for Inter-Bank Deposits.
				</p>
