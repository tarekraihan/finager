						<li>Cent Double Deposit Scheme:  8.75%</li>
						<li>Cent Super Plus term deposit:  9.30% (+.5% for seniors/+1% for staff)</li>
						<li>CENT Uttam Scheme:  As per prevailing term deposit rates</li>
						<li>Monthly Interest Deposit Receipt (MIDR):  As per prevailing term deposit rates (Monthly interest)</li>
						<li>CENT Tax Saving Deposit:  As per prevailing term deposit rates for 5 yrs (upto Rs.15 lakhs)</li>
						<li>Senior Citizen Deposit Scheme:  As per prevailing term deposit rates + 0.50% for seniors</li>
						<li>Money Multiplier Deposit Certificate (MMDC):  As per prevailing term deposit rates (interest is added onto the principal for a higher effective interest rate)</li>
						<li>Quarterly Interest Deposit Receipt (QIDR):  As per prevailing term deposit rates (Quarterly interest)</li>
