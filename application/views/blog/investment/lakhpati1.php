				<p>
					The Central Bank of India offers short and long term fixed deposits. Short- term fixed deposits are those term deposits where the term-to-maturity and interest rate 
					are fixed for a short period of time i.e. less than one year. Long-term fixed deposits are those term deposits where the term-to-maturity and interest rate are fixed 
					for a long period of time i.e. one year or more. The interest rates on fixed deposits are revised over time in accordance to market changes and bank requirements.
				</p>
				<p>
					Fixed deposits are gaining ground as an investment avenue in 2014 given the government's positive outlook on controlling inflation. Lower inflation makes fixed return
					products like fixed deposits more attractive as the returns are guaranteed and not diminished by inflation.
				</p>
				<p>
					The Central Bank of India's fixed deposit rates for 2014 are given below. It indicates both short-term and long-term fixed deposit rates in Central Bank of India.
				</p>
