					of the deposits by using a deposit rate calculator. Using this, the maturity value of the fixed deposit is returned on inputting the deposit amount for a particular 
					tenure at a particular rate. You can also then apply for the product that suits you the best. 
				</p>
				<p>
					Fixed deposit rates are different across banks because there are many factors that affect the rates set by each bank. Some key factors are: liquidity requirements, 
