					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Here’s how to open a money market account, a type of savings account that offers the security of FDIC insurance, the higher interest rates of a savings account and 
					the flexibility of access that comes with a checking account: 
				</p>
				<p>
					<b>• Shop around</b>. Find a bank or credit union that’s offering a competitive interest rate with the features you value the most — don’t just go for the bank down 
					the street or where you have your checking account (unless, of course, they are offering a great deal for bundling multiple accounts). For many consumers, the
					highest possible interest rate will be the biggest factor, but you’ll also want an interface that’s easy to use and solid customer service. If you want the ability 
					to write checks, be sure to ask. Some money market accounts allow limited check-writing privileges, others may not. It’s also important to know how many transactions 
					you’ll get before triggering a fee.
				</p>
				<br/>
				<p>
					<b>• Set your deposit amount</b>. Once you’ve settled on a bank or credit union, it’s time to figure out how much you want to put into your money market account. Money 
					market accounts require higher minimum balances than savings accounts. A typical deposit could be as low as $500 but also could be as high as a $10,000 minimum deposit.
					At the same bank or credit union, the minimum deposit for a savings account might be $5.
				</p>
				<br/>
				<p>
					<b>• Transfer the initial deposit amount</b>. Today this can be done quickly and electronically. Be aware of ongoing minimum balances to avoid fees.
				</p>
				<p>
					The goal for a money market account holder is to receive more interest with less risk. These accounts are historically favored by conservative investors who seek a 
					guaranteed return on their money. They are the “boring” part of many a balanced investment portfolio.
				</p>
				<br/>
				<h4><b>Top Savings Rates </b></h4>
				<p>
					A money-market account is like an intermediate stage between a savings account and certificate of deposit. It’s less liquid than a savings account, but more so than a CD.
				</p>
				<p>
					Finally, it’s important to note that there’s a difference between money market accounts and money market funds, which are more like mutual funds and are not backed by 
					FDIC insurance. Further, you cannot withdraw your money as easily.
				</p>
				<p>
					In the end, I’m not sure it’s important for the average person to use the mental energy to differentiate between money market accounts and other accounts. As long as 
					you search for the best interest rates for your particular needs, you should be fine, regardless of whether your use a savings account or a money market account.
				</p>
