				<div class="details_heading">
					<h3>Post Office Monthly Income Scheme</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Most of the people, mostly in urban areas, if asked about money and post office will think of money order or at most National Saving
					Certificate(NSC). But Indian post offices provide a lot of other options to invest your money.
				</p>
				<p>
					Apart from banks; Public Provident Fund, Sukanaya Samriddhi Yojana account and KVP can be opened in post offices too.
				</p>
				<p>
					There are other investment schemes too which are specific to post office like NSC, Senior Citizen Savings Scheme (SCSS) account, Monthly Income 
					Scheme (MIS) account. This post is to give information about Monthly Income Scheme.
				</p>
				<br/>
				<h4><b>Post Office Monthly Income Scheme</b></h4>
				<p>
					Monthly Income Scheme an investment scheme from Indian Post provides fixed monthly income at the given rate of interest (8.4% in FY 2015-16, 7.8% 
					from April 1, 2016). It is a good scheme for risk-averse investors who want fixed income free from any fluctuations.
				</p>
				<p>
					It is ideally suited for senior citizens and retired people who have got some lump-sum amount after retirement. A portion of that amount can 
					be invested in monthly income scheme to get monthly payments. You have to compare it with quarterly interest paying fixed deposits to see which
					gives better return.
				</p><br/>
				<h4><b>Opening Monthly Income Scheme (MIS) account</b></h4>
				<p>
					Account may be opened by individual. Joint account is also permitted and can be opened by two or three adults. In case of joint account all joint
					account holders have equal share in each joint account.
				</p>
				<p>
					Also single account can be converted into Joint and Vice Versa.
				</p>
				<p>
					Account can be opened by cash/cheque and in case of cheque the date of realization of cheque in Govt. account shall be date of opening of account.
				</p><br/>
				<h4><b>Required documents</b></h4>
				<p>
					<ul style="margin-left:15px;">
						<li>At the time of opening POMIS you need to submit - Filled account opening form (provided by post office where you are opening the account).</li>
						<li>Copy of the address proof and identity proof like (passport/PAN card/ration card/voter identity card).</li>
						<li>Two passport size photographs.</li>
						<li>You need to take the originals with you for verification.</li>
						<li>Eligibility for opening MIS account</li>
						<li>Monthly income scheme is only for Resident Indians, NRIs can't invest in it.</li>
						<li>Account can be opened in the name of minor and a minor of 10 years and above age can open and operate the account.</li>
						<li>Minor after attaining majority has to apply for conversion of the account in his name.</li>
					</ul>
				</p>
				<br/>
				<h4><b>Minimum and maximum limit on investment</b></h4>
				<p>
					<ul style="margin-left:15px;">
						<li>Investment should be in multiples of INR 1500/- with maximum investment limit as INR 4.5 lakhs in single account and INR 9 lakhs in joint account.</li>
						<li>Remember that an individual can invest maximum INR 4.5 lakh in MIS (including his share in joint accounts)</li>
						<li>For calculation of share of an individual in joint account, each joint holder have equal share in each joint account.</li>
					</ul>
				</p>
				<br/>
				<h4><b>Interest rate</b></h4>
				<p>
					<ul style="margin-left:15px;">
						<li>As per recent announcement from 1-4-2016, interest rate is going to be 7.8% per annum payable monthly.</li>
						<li>Till 31-03-2015, interest rate is 8.40% per annum payable monthly.</li>
						<li>So let's see an example with the interest rate as 7.8%.</li>
						<li>If you have invested Rs. 1,50,000 (Rs. 1.5 Lakhs) in the POMIS then with annual interest rate as 7.8% annual interest income is - Rs. 11,700</li>
						<li>So monthly pay-out would be - Rs. 975</li>
						<li>With interest rate as 8.4% it was Rs. 1050.</li>
						<li>Interest can be drawn through auto credit into savings account standing at same post office, through PDCs or ECS./In case of MIS accounts standing at CBS (Core Banking Solution) Post offices, monthly interest can be credited into savings account standing at any CBS Post offices.</li>
					</ul>
				</p>
				<br/>
				<h4><b>Tax on MIS</b></h4>
				<p>
					<ul style="margin-left:15px;">
						<li>Post office monthly income scheme is not eligible for deduction under Sec 80C.</li>
						<li>Also note that amount received as monthly income from this scheme will be added to your income and taxed according to the slab you fall in.</li>
						<br/>
						Point to note here -
						<li>If you are not withdrawing the monthly pay-outs, that amount does not yield any interest.</li>
						<li>There is no TDS on the Post Office MIS, as mentioned above interest income is taxable in your hands.</li>
					</ul>
				</p>
				<br/>
				<h4><b>Maturity</b></h4>
				<p>
					<ul style="margin-left:15px;">
						<li>Maturity period is 5 year.</li>
						<li>Pre-mature closure</li>
						<li>Can be prematurely en-cashed after one year, rule is as</li>
						<li>After one year but before 3 years at the discount of 2% of the deposit and After 3 years at the discount of 1% of the deposit.</li>
						<li>After 3 years at the discount of 1% of the deposit.</li>
						<li>Here discount means deduction from the deposit.</li>
					</ul>	
				</p>
				<br/>
				<h4><b>Transferable</b></h4>
				<p>
					Account can be transferred from one post office to another.
				</p>
				<br/>
				<h4><b>Nomination Facility</b></h4>
				<p>
					Nomination facility is available at the time of opening the MIS account. You can also do the nomination after opening of account.
				</p>
				<br/>
				<h4><b>Bonus</b></h4>
				<p>
					Giving bonus at the time of maturity is discontinued.
				</p>
				<p>
					There was a bonus of 5% on principal amount at the time of maturity of MIS accounts opened on or after 8- 12-07 and up to 30-11-2011. No bonus is payable on the deposits made on or after 1-12-2011.
				</p>
