				<div class="details_heading">
					<h3>Tenancy Deposit Scheme charges – an update</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<h4><b>Tenancy Deposit Scheme</b></h4>
				<p>
					As everyone involved in the private rented sector should be aware, tenancy deposits taken for assured shorthold tenancies now need to be protected with a government
					approved tenancy deposit scheme.  All schemes are required to provide a free arbitration service, in the event of a dispute regarding deductions from the deposit 
					at the end of the tenancy.  The tenancy deposit regulations came into force in April 2007.
				</p><br/>
				<h4><b>The authorised tenancy deposit scheme companies</b></h4>
				<p>
					We have three government authorised tenancy deposit companies and three methods of charging:
				</p>
				<p>
					<b>1. The DPS</b> are free to everyone, their running costs are met from interest on the deposits they hold
				</p>
				<p>
					<b>2. My Deposits</b> charge a joining fee, an annual membership fee, and a fee per deposit protected (which, for landlords, is cheaper if done online).  The basic fees
					for agents are more expensive, but agents can get significant discounts by pre purchasing blocks of protections
				</p>
				<p>
					<b>3. TDS</b> 3. TDS (confusingly standing both for The Dispute Service and Tenancy Deposit Scheme) charge agents an annual flat fee which varies depending on
					the organisation regulating the agent and the number of tenancies registered at a specific date in the year.  There is a sliding scale of discounts for firms 
					which have generated few or no disputes.  The fees for landlords are, we are told, calculated on an individual basis.  Only agents who are regulated by specified
					organisations (ARLA, NAEA, RICS, NALS and the Law Society) are permitted to be members.
				</p>
				<br/>
				<h4><b>Problems with TDS</b></h4>
				<p>
					As most people in the private rented sector are aware, there have been a number of high profile problems with the TDS model which has necessitated changes:
					<ul style="margin-left:15px;">
						<li>In January 2009 they withdrew membership from unregulated members, a move apparently forced on them by their insurers who had taken some heavy knocks from unregulated agents going bust.</li>
						<li>Last December they were reported to be dramatically reducing the number of adjudicators used</li>
						<li>This year agents have been rocked by massive hikes in their annual charges.   This, we are told, is mainly due to the large number of disputes being generated, sometimes it seems  for ridiculously low sums.</li>
					</ul>
				</p>
				<br/>
				<h4><b>Comments on the TDS fee increase</b></h4>
				<p>
					As can be imagined the fee increase has caused considerable hardship for many agents.  TDS somewhat glibly say on their site:
				</p>
				<p>
					TDS was designed specifically for regulated agents and corporate landlords. We are confident that it remains the cheapest insured option for deposit protection for the majority of its members.
				</p>
				<p>
					However many of their members may beg leave to differ. For example see some of the comments on this blog, such as those from:
					<ul style="margin-left:15px;">
						<li>Kevin Burge, whose firm rent:cheltenham is a licensed ARLA agent which referred no disputes to arbitration, but which have had their annual fees increased by 23%, and who has big criticisms of the dispute system</li>
						<li>An anonymous commentator from a licensed ARLA firm, who reported that they had had 1 dispute for every 187 tenancies and have had their fee increased by over 1,000% (and say TDS refuse to say how this was calculated)</li>
						<li>Jonny from David Martin Estate Agents, who reported that his firm had about “350 deposits registered, 3 disputes and the fee is going from £583.00 to £3400.00”.  A charge effectively to them of £939 per dispute, and</li>
						<li>Natalie from Temples, a NALS agency, who reported that her firms fee had increased from £818  to £3206 based on 274 tenancies and 1 registered dispute.  She pointed out that the dispute was over a £150 cleaning bills which the tenant had insisted on challenging and which had been found in favour of the landlord (who was therefore justified in refusing to settle).  However it seems that her firms is now paying the price of some £2,388 for this!  Again she reports that TDS have failed to explain the reason for this 292% increase (based it would seem on the one dispute).</li>
					</ul>
					The fee increases have even been the subject of questions in Parliament as this exchange on 23 March shows.
				</p>
				<br/>
				<h4><b>Grant Shapps:</b></h4>
				<p>
					To ask the Secretary of State for Communities and Local Government whether his Department’s contracts with tenancy deposit scheme providers provide for it to
					intervene to prevent an increase in the level of fees charged by a provider.
				</p>
				<p>
					<b>Mr. Ian Austin</b>: The Department has no role in the level of fees that the tenancy deposit scheme providers charge their members. Any increase is a commercial decision for the scheme providers.
				</p>
				<br/>
				<h4><b>What about moving to another tenancy deposit scheme?</b></h4>
				<p>
					Not surprisingly, many agents are now thinking of moving to one of the other two companies, although the commentators on my previous post were apprehensive about
					the cost of this and the extra administration.
				</p>
				<p>
					However, in the last few days, My Deposits have reported  that Countrywide Residential Lettings, the UKs largest letting agent, has moved to using their 
					service with effect from 1 April 2010.  Quite a coup!
				</p>
				<p>
					MyDeposits now publish a special Q&A pdf download on their website setting out answers to frequently asked questions.  This is very interesting, particularly Q18.:
				</p>
				<p>
					Unlike TDS, we require the main correspondence address of  the landlord (we do not accept ‘C/O’ addresses or PO Boxes) so that we can explain to them the principles 
					of the legislation and what penalties they face if you, the agent, fail to protect the deposit or have your membership cancelled. If you do not wish us to contact your 
					landlord for this purpose then we regret my|deposits is not suitable for your needs.
				</p>
				<p>
					We undertake annual fraud and financial credit checks on all our agent members including the request for evidence of segregated client money accounts and balances 
					within these accounts that match the level of deposit protection we provide. Failure to provide this evidence will invalidate membership.
				</p>
				<p>
					It does seem, as they are not shy of stating, that My Deposits charging model is more robust than that used by TDS.  My Deposits were always intended to be the 
					‘landlords tenancy deposit company’ and state in their Q&A that at present that they have approximately 50,000 landlord members to 3,000 agent members.  However 
					this may change.  Most letting agents have gone through a very tough time with the credit crunch and are not in a position to easily absorb cost increases of 23% 
					let alone 200-1,000%!
				</p>
				<p>
					The ‘free’ DPS service is often not favoured by agents, as they prefer to hold the deposits in house, partly because of the interest income, and partly because it 
					is easier for them administratively.  However for those considering changing Kevin First of the DPS, comments
				</p>
				<p>
					DPS does now offer bulk uploads – if you email the scheme we will send you the template. It’s very straightforward, you just download your tenancies into the template
					and send it back to us with your DPS Agent ID and we will upload the data into your account. Once you have transferred the cash all deposits will become active and
					deposit confirmations will be emailed to all parties – so there will be no gap in protection.
				</p>
