					<h3>Where do we stand currently with tenancy deposits?</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Deposit protection was introduced eight years ago, but some confusion still remains about a landlord’s obligations. In this month’s column, Alex Byard brings us up to date…
				</p><br/>
				<h4><b>Where do we stand currently with tenancy deposits?</b></h4>
				<p>
					As we have discussed many times before, it has been mandatory for residential landlords to protect deposits taken against a new assured shorthold tenancy since April 2007.
				</p>
				<p>
					It is important to note that the law does not apply to any other types of tenancy, but you cannot contract out of the obligation. The Housing Act 2004 
					states: “Any tenancy deposit paid to a person in connection with an assured shorthold tenancy must, as from the time when it is received, be dealt with 
					in accordance with an authorised scheme.”
				</p>
				<p>
					The purpose of this legislation is to safeguard deposits and facilitate the fair resolution of disputes, should they arise, when the tenant moves out.
				</p>
				<br/>
				<h4><b>What constitutes an authorised scheme?</b></h4>
				<p>
					There are currently three administrators for the tenancy deposit scheme and you can choose which to use.
				</p>
				<p>
					The first is the Deposit Protection Service (DPS). It is best known for its custodial scheme, but an insurance-based alternative is available. Landlords who 
					lodge their deposits with the DPS have 30 days to do so and the money stays there until the tenancy comes to an end.
				</p>
				<p>
					By taking out an approved insurance policy you can retain deposits, in return for a fee and annual premium. The profits collected by the administrator are 
					used to pay tenants, should their landlord misappropriate the money.
				</p>
				<p>
					A second insurance-based scheme was set up by the Dispute Service. It is supported by the Association of Residential Lettings Agents, the National Association 
					of Estate Agents, the Residential Landlords’ Association and the Royal Institute of Chartered Surveyors.
				</p>
				<p>
					The final administrator is MyDeposit, operated by Tenancy Deposit Solutions. It too started life offering insurance, but earlier this month, was given the green 
					light to launch a custodial alternative to the DPS from April next year.
				</p>
				<br/>
				<h4><b>A landlord’s obligations</b></h4>
				<p>
					If you take a deposit from your tenant you are not only obliged to protect it in one of the schemes outlined, but also provide prescribed information 
					about your chosen provider within 30 days.
				</p>
				<p>
					Prescribed information must be given to the tenant and any ‘relevant person’ i.e. someone who pays the deposit on behalf of the tenant, in accordance with your 
					agreement. There is no standard format, but it must contain your name and contact details, the name of the scheme administrator you are using and a leaflet
					outlining its services that has been certified by you. Failure to cover all of these points is a breach of the legislation and it will be deemed that the 
					prescribed information has not been served.
				</p>
				<p>
					There are significant sanctions for noncompliance with either scheme. You may be prevented from recovering possession of your property and/or required to pay money 
					to the tenant or relevant person by way of a fine.
				</p>
				<p>
					Although there is no obligation to take a deposit from your tenant, it is the best form of security against damage to your property. If you intend to do so, 
					it is imperative the money is protected and the prescribed information delivered in an appropriate manner.
				</p>
				
