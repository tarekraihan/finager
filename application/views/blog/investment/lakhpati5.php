					<h3>Demonetisation: Investigate unusual spurt in deposits</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					A graphic on this page on Thursday showed a steep increase of Rs 4.8 lakh crore in bank deposits in September over the August figure. This spurt, ahead of the 
					demonetisation of Rs 1,000 and Rs 500 notes announced on November 8, was not seasonal. It needs to be inquired into, to rid the demonetisation drive of a possible 
					taint in the form of suspicion that some people took advantage of insider information. The Opposition is likely to point fingers at the ruling party but it would be 
					strange, indeed, if those in the know of a move planned six months ahead waited till September to act. Information could have leaked out, if it did leak out, from the
					RBI or the printing presses. The matter calls for an explanation.
				</p>
				<br/>
				<p>
					Initially, it had been suggested that the September surge was on account of disbursal of Pay Commission arrears. But that was only some Rs 34,000 crore. Spectrum 
					auction proceeds accounted for another Rs 32,000 crore. Allow for some bunched subsidy payments. That would leave unexplained a jump in deposits of Rs 3 lakh crore. 
					It is easy to establish if anyone took advantage of prior information of the demonetisation move and converted high-denomination notes into small-denomination ones. 
					Look at the flow of big and small notes between the banks and the central bank in each of the major states over every quarter since April 2014. The RBI keeps track 
					of this data. Former banker T R Ramaswami suggests, in a letter to the editor, an examination of the inflow of Rs 500 and Rs 1,000 notes into the RBI and the outflow 
					of Rs 50 and Rs 100 notes from the central bank to banks for five six-month periods, starting April 2014. If there is any break, in the first half of the current 
					fiscal year, in the pattern of flow of big notes into, and of small notes from, the central bank, it would indicate foul play. If the pattern is the same from April
					2014 onwards, that would rule out mischief.
				</p>
				<br/>
				<p>
					It would be in the government’s interest to initiate an inquiry on the above lines, without waiting for the Opposition to raise the demand, or for the judiciary to 
					order one, in response to a public interest litigation.
				</p>
