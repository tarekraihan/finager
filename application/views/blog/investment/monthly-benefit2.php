				<div class="details_heading">
					<h3>Monthly Income Fixed Deposit</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					When it comes to safe investments, there is no doubt about the fact that a fixed deposit is a very attractive option since it provides safety
					coupled with steady growth. Some fixed deposits also go so far as to offer income tax benefits on the amounts invested in them each year. However, 
					there are times when the people who invest in fixed deposits need to maintain constant access to that investment so as to be able to augment their 
					monthly income. Taking these needs into consideration some banks offer specific fixed deposits that are known as monthly income plans and help 
					augment their monthly income.
				</p>
				<br/>
				<h4><b>What is a monthly income fixed deposit?</b></h4>
				<p>
					What a monthly income fixed deposit is basically a fixed deposit that can be created and earns an interest in much the same way a regular fixed 
					deposit does with a few differences. The being that with a monthly income plan the investors either have access to the amount deposited or are 
					paid the interest earned on a monthly basis. Some banks even let depositors withdraw the interest every month from regular fixed deposits at 
					discounted interest rates. The monthly income plans are generally linked to a bank account and the interest earned is credited into that account.
				</p><br/>
				<h4><b>Features of the monthly income fixed deposit</b></h4>
				<p>
					While the features of these accounts can vary from one bank to another, a general set of features that you may see with these accounts are:
					<ul style="margin-left:15px;">
						<li>With some banks, there is a minimum and a maximum amount that can be deposited in these accounts.</li>
						<li>Some banks even offer the option of monthly, quarterly or half yearly income plans.</li>
						<li>Some of these products come with credit ratings that form a good indicator of how good the plan is.</li>
						<li>These deposits allow for premature withdrawals to be made from them.</li>
						<li>Almost all deposits provide nomination facilities.</li>
						<li>The duration of these deposits can go all the way up to 10 years.</li>
						<li>Some banks permit depositors to withdraw as much as 95% of the amount deposited.</li>
						<li>Some of these deposits come with overdraft facilities on the savings/current accounts linked to them and any excess money needed by the account is withdrawn from the FD.</li>
						<li>With some banks, no TDS is deducted by the banks on the interest earned.</li>
					</ul>
				</p><br/>
				<h4><b>Rate of Interest on Monthly Income Scheme</b></h4>
				<p>
					The interest rates on these accounts tend to follow the same rates as those prescribed for the regular term deposits. They also tend to offer
					senior citizens a higher interest rate which can be anything from 0.25% to 0.5% over the applicable rates. These rates are decided by the banks so
					it’s best to confirm the exact rate with the banks before investing. With some banks, current and past employees of that banks also get higher
					interest rates.
				</p>
				<p>
					If you are using a regular fixed deposit and withdrawing the interest every month to augment your income, you should know that sometimes banks 
					will provide discounted interest rates.
				</p><br/>
				<h4><b>Eligibility criteria and documents required</b></h4>
				<p>
					The eligibility criteria for these deposits follow the same rules as those for the regular term deposits where any individual, company, HUF or
					association of people can open the fixed deposits. Even NRIs are eligible to open monthly income FDs.
				</p>
				<p>
					The documents required also tend to be the same and will include the application form along KYC documents like an identity proof, address proof etc.
				</p>
				<p>
					The biggest advantage of the monthly income fixed deposit is that with these products, if depositors need extra money in a month, they don’t have
					to liquidate that entire deposit. They can just withdraw what they need.
				</p>
