					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Deposits from over 3,500 tenants have been protected during the first year of the States’ approved tenancy deposit scheme, mydeposits Jersey. Since 2 November 2015,
					landlords and letting agents have been legally required to protect their tenants’ deposits by placing them in the mydeposits Jersey scheme. Under the law all landlords
					and agents who take a deposit from their tenant on a new, renewed or varied tenancy agreement must transfer the money to mydeposits within 30 working days of receiving
					it. Those who fail to do so could be fined up to £2,000.
				</p>
				<p>
					Housing Minister, Deputy Anne Pryke, said: “I am immensely pleased with the response to the scheme from landlords and tenants over the last year. More than 3,500 tenant
					deposits have been received so far from over 1,100 landlords and agents, and this will increase further over the next few years as more tenancies fall under the 
					protection of the scheme.”
				</p>
				<p>
					A recent survey carried out by mydeposits Jersey showed that awareness among renters was strong, with over 90% of tenants confirming they were aware of the scheme and how it protects them.
				</p>
				<p>
					The chief executive officer of mydeposits, Eddie Hooker, said: “It is great to see so many tenants supporting this important initiative from the States of Jersey. 
					Tenancy deposit protection is designed to ensure tenants get their deposit back when they’re entitled to it at the end of the tenancy.”
				</p>
				<p>
					The scheme is one part of the work being done to improve the rental sector in Jersey, including the recent launch of the Rent Safe landlord accreditation scheme.
					Deposit protection aims to give tenants greater confidence that their deposit money is being held securely and will be repaid at the end of an agreement. mydeposits 
					Jersey also offers a free dispute resolution service for landlords and tenants if they experience any deposit related issues at the end of their tenancy.
				</p>
				<p>
					Deputy Pryke said: “Landlords are legally obliged to protect their tenant’s deposit when they create a new lease or renew or vary an existing lease, and that they
					should register with my deposits as soon as possible.”
