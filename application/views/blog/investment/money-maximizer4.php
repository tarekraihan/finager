					<h3>What is a Certificate of Deposit (CD)?</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Sold by banks, certificates of deposit (better known as CDs) are low-risk –- and relatively low-return — investments suitable for cash you don’t need for months or 
					years. If you leave the money alone during the investment period (known as the “term” or “duration”), the bank will pay you an interest rate slightly higher than what 
					you would have earned in a money market or checking account. All gains from CDs are taxable as income, unless they are in a tax-deferred (IRA) or tax-free (Roth IRA) 
					account.
				</p>
				<p>
					CDs are among the safest investment a persona can make. The interest rate is determined ahead of time, and you’re guaranteed to get back what you put in, plus interest
					once the CD matures. What’s more, if the bank goes belly up, your deposit is probably insured by the FDIC for up to $250,000.
				</p>
				<br/>
				<p>
					Here are the most common types of CDs:
				</p>
				<br/>
				<p>
					<b>• Traditional CD:</b> You receive a fixed interest rate over a specific period of time. When that term ends, you can withdraw your money or roll it into another CD. Withdrawing before maturity can result in a hefty penalty.
				</p>
				<br/>
				<p>
					<b>• Bump-Up CD:</b> This kind of account allows you to swap your CD’s interest rate for a higher one if rates on new CDs of similar duration rise during your investment
					period. Most institutions that offer this type of CD let you bump up once during the term of your CD and keep the interest rate for the remainder of the original
					CDs term.
				</p>
				<br/>
				<p>
					<b>• Liquid CD</b> — This kind of account allows you to withdraw part of your deposit without paying a penalty. The interest rate on this CD usually is a little lower than 
					others, but the rate is still higher than the rate in a money market account.
				</p>
				<br/>
				<p>
					<b>• Zero-coupon CD</b> — This kind of CD does not pay out annual interest, and instead re-invests the payments so you earn interest on a higher total deposit. The interest
					rate offered is slightly higher than other CDs, but you’ll owe taxes on the re-invested interest.
				</p>
				<br/>
				<p>
					<b>• Callable CD</b> — A bank that issues this kind of CD can recall it after a set period, returning your deposit plus any interest owed. Banks do this when interest rates 
					fall significantly below the rate initially offered. To make this type of CD attractive, banks typically pay a higher interest rate. These accounts are typically offered through brokerages.
				</p>
				<br/>
				<p>
					<b>• Brokered CD</b> — This term refers to any CD offered by a brokerage. Brokerages have access to thousands of banks’ CD offerings, including online banks. Brokered CDs will
					generally carry a higher rate of interest from online and smaller banks because they’re competing nationally for depositors’ dollars. However, you’ll pay a fee to 
					purchase the account.
				</p>
