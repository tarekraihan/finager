				<div class="details_heading">
					<h3>KEY CONSIDERATIONS FOR GRABBING THE BEST CAR LOANS</h3>
					<h4><i>Writen by Sujat, 02-Oct-2016</i></h4>
				</div>
				<p><span class="uper_text">A</span>re you thinking about buying a new car or used car? Well then, 
				the first thing that will assist you to get your dream car is a car loan. You need to know how many 
				kinds of vehicle loans are there and how to apply for the one that you need.
				</p>
				<br>
				<h4><b>Here are the things that you need to give forethought when buying a new or used car</b></h4>
				<p>
					<ul style="margin-left:12px;">
						<li>What loan amount will you get?</li>
						<li>What is the loan type?</li>
						<li>What type of car you want to buy?</li>
						<li>What special features do you need?</li>
						<li>The final checklist</li>
					</ul>
				</p><br>
				<h4><b>1. What loan amount will you get?</b></h4>
				<p>
					The loan amount will depend on the cost of the car that you want to buy plus what amount of money you will invest from your 
					own pocket for buying the car. Above all, it will depend on what amount you can afford to make monthly repayments. 
					Thus, you need to work out a monthly budget to see the amount of money you’ll be able to afford for paying towards <b>your
					new or used car loan</b>. Some lenders have a base and ceiling for the loan amount from which you can borrow so you need to ensure borrowing within their limits.
				</p><br>
				<h4><b>2. What is the loan type?</b></h4>
				<p>
					Know the different types of easy car loans so you can identify the right one.
				</p>
				<h4><b>• Variable:</b></h4>
				<p>
					The interest rates of a variable loan amount keep on changing, which may affect your loan repayment amount. For instance; 
					if your finance company lowers the interest rate, your repayments will decrease and if they increase the interest rates,
					your repayments will increase. This type of loan is difficult to budget for as your repayments keep on changing every month;
					nonetheless, the interest rates are usually lower than fixed rates.
				</p>
				<h4><b>• Fixed:</b></h4>
				<p>
					The interest rates of the fixed loan amount remain the same throughout the loan’s tenure, which means your monthly loan repayments
					remain the same throughout. Thus, it is easier for you to budget for your monthly repayments for fixed loan type compared to a variable;
					nevertheless, fixed loan’s interest rates tend to be on the higher side than variable loan’s interest rates.
				</p>
				<h4><b>• Secured: </b></h4>
				<p>
					Usually, the car you desire to buy is used as an asset for security against the loan you get so it is called a secured car loan.
					This loan type has a lower risk for lenders because if you default on your repayments, they can take back the possession of your 
					vehicle and sell it away so that they can pay off your loan. It normally provides better interest rates.
				</p>
				<h4><b>• Unsecured:</b></h4>
				<p>
					With an unsecured car loan, you don’t have to put your car as a security for the loan, but you need to prove that you can meet the repayments 
					by showing a history of your savings or if you have previously had a loan or a credit card and that you completed the repayments.
				</p><br>
				<h4><b>3. What type of car you want to buy?</b></h4>
				<p>
					The type of <b>car finance</b> you need will depend on the car that you want to buy such as a new car or a used car. In the case of a used car, 
					the age of the vehicle will also be taken into consideration.
				</p>
				<h4><b>• Loan for New Car:</b></h4>
				<p>
					If you’re looking to purchase a new car then you need to look at new car loans. You can avail a personal loan type that is mainly accessible 
					for new car purchases. As new cars are considered less risky for the lender, the interest rates are normally lower for new cars.
				</p>
				<h4><b>• Loan for Used Car:</b></h4>
				<p>
					In order to purchase a used car, you’ll need a used car loan. However, there may be limitations on the age of the car for it to become 
					eligible for a used car loan. For instance, some may provide this type of loan to cars that are between 2 and 5 years old. Thus, it’s always
					recommended to check with your lender for the fine print (terms & conditions). Secondly, the interest rate would be little higher for a used 
					car when compared to a new car. Nonetheless, similar to a new car loan, there are also other options available to select from such as secured 
					and unsecured used car loans.
				</p><br>
				<h4><b>4. Desired Features</b></h4>
				<p>
					When determining your options for the <b>best car loans</b>, ensure looking into the features of the loan. 
					There are an array of features attached to car loans, which are all particular to the type of loan and the lender. Here are some of the desired features:
				</p>
				<h4><b>• Additional repayments:</b></h4>
				<p>
					You need to know whether you’ll be able to make additional repayments for your car loan. If you are allowed to do this, 
					you’ll be able to complete the loan repayments sooner and save money. You’ll need to ask your lender if your car loan
					facilitates additional repayments – and whether you will be charged for additional repayments and also if there is any limit on how much
					repayments are allowed during a certain time frame. Prior to applying for a loan, these are the fine print that you should know.
				</p>
				<h4><b>• No penalty for early repayments:</b></h4>
				<p>
					Always enquire about this feature. In case, you come across excess money and want to pay off your loan before its due date, 
					would you be able to do it without getting penalised for the same. This will vary from lender to lender and the loan type you
					apply for so ensure you enquire about this feature.
				</p><br>
				<h4><b>5. The Final Checklist</b></h4>
				<p>
					Here, we have compiled a final checklist of some of the documentation so that the process becomes easier for you, which may be required to support your car loan application.
				</p>
				<h4><b>• Identification:</b></h4>
				<p>
					Normally, when applying for a car loan, you have to furnish identification proof in accord with the ID check system.
					So, keep these things ready – your driver’s license or passport and your birth certificate and Medicare card or any 
					two added cards that have your name and photo on them so as to prove your identity.
				</p>
				<h4><b>• Income proof:</b></h4>
				<p>
					You will need to furnish pay slips from the past few months to prove you earn the amount that you mention on your application. 
					If you don’t have copies of these, you may be able to use your most recent tax return, but you’ll have to check with your lender to know if they will acknowledge these.
				</p>
				<h4><b>• Bank statements:</b></h4>
				<p>
					Lenders may also ask to see your bank statement copies. Most of the lenders will ask for copies of your bank statements from 
					the past months to find out that you hold a savings history. If you don’t receive paper statements any longer, you can log into your 
					Internet banking and print them out or also even better, you can email them to your lender.
				</p>
				<h4><b>• Car details:</b></h4>
				<p>
					Suppose, you’re using your car to secure the loan, you’ll require giving details of the car like engine and registration numbers.
					If you’re buying from a dealer, your lender may need to make a cheque directly to them so you may have to furnish details to your lender. 
					Additionally, when you buy it, you may need to request for receipt’s copy to prove the cost.
				</p>
				<h4><b>• Credit history records:</b></h4>
				<p>
					While it can feel a bit interfering, lenders have to check your credit history so that they can come to a conclusion that
					you’ll be able to make the loan repayments. If you have other loans or credit cards, they may request you to see copies 
					of your past few statements for proof that you can handle the repayments and haven’t defaulted. However, if you’ve never 
					held a credit card or loan before, do not get troubled because your income proof and your electricity and phone bills and 
					also your rental payment receipts can all be considered for your credit history.
				</p>
				<h4><b>• Contact details:</b></h4>
				<p>
					You have to ensure that you have your own contact details such as your full residential address – your phone numbers etc. 
					so that your loan application process gets completed smoothly without any obstacles.
				</p>
