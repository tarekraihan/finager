				<p>
					Most online lenders do not charge a fee for paying off the loan before a certain date, called prepayment penalties or exit fees. 
					But just to be sure, always look for the words “no prepayment penalty” on your loan terms when you apply.
				</p><br/>
				<h4><b>Accidental overdrafts</b></h4>
				<p>
					Many online lenders ask for automatic withdrawals from your checking account, or offer a lower APR for choosing this option instead of paying by check. 
					If you link your loan to your checking account for automatic payments, you might be in danger of overdrawing your account and paying an overdraft fee — usually,
					about $35.  To avoid accidentally draining your bank account, consider setting up a low balance alert with your bank.
				</p><br/>
				<h4><b>Scam artists</b></h4>
				<p>
					Before you sign up for any loan, particularly online, check out the Better Business Bureau and Federal Trade Commission to make sure the organization is legitimate.
					If you borrow from a lender who has a record of unfair practices or charges usurious rates, you may have a hard time getting out of debt.
