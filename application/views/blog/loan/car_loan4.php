					<h3>Car Loans</h3>
					<h4><i>Writen by Sujat, 02-Oct-2016</i></h4>
				</div>
				<p><span class="uper_text">B</span>eing able to travel at godlike speed is nothing short of marvellous. 
					Owning a car has moved from being a status symbol to being a necessity. It affords us time and comfort 
					along with the luxury of spending time with our dear ones as we commute. While the demand for a personally 
					owned car has increased, figuring out how you are going to finance a car is far more important. It is more important than finding the perfect car model.
				</p>
				<p>
					Having the finance in place ensures you being ready when the right deal presents itself. 
					It also increases your chances of cruising along the road that much quickly. Most banks, 
					financial institutes, NBFCs or specialist car financing companies provide customers with a car loan.
					The idea is just as it sounds – loans given for the purchase of a car. You can get cars loans for both, 
					new cars and used cars. There are also some car manufacturing companies with their own car financing firms that finance customers to buy their cars.
				</p>
				<br>
				<h4><b>Pros of taking a car loan</b></h4>
				<p>
					<ol style="margin-left:12px;" type="a">
						<li>The lending institutes can provide you with a flexible contract term, ranging from 2 to 10 years. This can make it easy for you to manage your monthly finances while availing the benefits of owning a car.</li>
						<li>You can reduce the monthly instalments by applying a residual to the loan.</li>
						<li>Lending institutes can give you a choice between fixed and variable rate of interest.</li>
						<li>You become eligible for tax deduction if your car is going to be used for business purposes.</li>
						<li>Usually, lower rates of interest are available as the loan is secured against the car.</li>
					</ol>
				</p><br>
				<p>
					Applying for a car loan is very simple. There are a host of financial firms with web portals that provide handheld 
					services beginning at assessing your eligibility, informing about the documents required and the application process, 
					submitting or uploading the documents online, confidentially, and calculating EMIs for planning and management of your monthly finances. 
					You simply have to log online to get all the car loan related information. Remember though, that even if the process for buying
					a new and a used car is quite similar, the rate of interest of used cars is much higher than new cars, making them a costly affair in the long run.
				</p><br>
				<h4><b>Whichever car loan you are considering, it is necessary to be aware of the following things:</b></h4>
				<h4><b>Current financial status:</b></h4>
				<p>
					If you have been careful with your budgeting, then you would probably be aware of the down payment you can make for the car, 
					the amount of loan you require, and the EMIs post loan approval. However, for those who do not put a lot of thought into this matter, 
					it is important to become aware of your monthly income, expenses and savings. Since your loan approval and rate of interest is dependent 
					on your monthly income and credit score (discussed below), knowing your current financial status becomes crucial.
					It also prevents the agents from confusing you with complicated math and calculations and giving you a higher rate of interest. 
					This will also ensure that you do not end up paying more rate of interest than the actual monetary worth of the car.
				</p><br>
				<h4><b>Credit score:</b></h4>
				<p>
					Credit score is the rating you get from a Credit Information Company (CIC) based on your annual income, past loan repayments,
					and credit card statements. CIBIL (Credit Information Bureau India Limited) is one such institute that can help you access your credit score.
					A good credit score is essential to get a lower rate of interest and to get the loan sanctioned quicker.
				</p><br>
				<h4><b>What will the buy price be at the end of the lease and how can I buy the car?</b></h4>
				<p>
					During the lease negotiation the worth of the car at the end of the lease is set as part of the lease. 
					As the lessee you usually have the option to buy the car at the end of the lease, so be sure to pay attention to this part of 
					the lease negotiation. Usually you must give the dealership at least a month’s notice that you intend to purchase the vehicle 
					prior to the end of the lease. The buy price listed in the lease is often negotiable at the end of the lease based on the value of 
					the car at that given time. Additionally, buying your leased vehicle is an attractive option if you are facing excessive mileage or 
					wear and tear fees at the end of your lease.
				</p><br>
				<h4><b>Rate of interest: </b></h4>
				<p>
					Banks and other lending institutes fix the rate of interest depending on the type of car and loan amount you are choosing.
					Interest rates are usually fixed for car loans. Keep in mind that interest rate for a used car is about 20% higher.
				</p><br>
				<h4><b>Tenure:</b></h4>
				<p>
					This means the duration for which you will be paying the EMIs, which includes the interest rate. It can range from 1 year up to 10 years.
					It is advisable to make a hefty down payment in order to lessen the loan repayment duration and avoid paying more than the car is worth. 
					The maximum tenure for used cars is only 36 months, which puts higher EMI burden on the customer.
				</p>
				<h4><b>Car loans are secured:</b></h4>
				<p>
					In that, the banks or the financial institution approve your loan against the car that you are purchasing.
					Typically, a few delayed payments are allowed by incurring penalty fees. But if you consistently fail to make your monthly payments (EMIs),
					then the institutes, verbally and via written communication, request to begin payments immediately. If you still do not make the payments, 
					the institutes have the right to repossess your car.
				</p>
				<h4><b>Documents required for applying for a car loan are as follows:</b></h4>
				<p>
					<ol style="margin-left:15px;" type="a">
						<li>Age proof</li>
						<li>ID proof</li>
						<li>Application form</li>
						<li>Photograph</li>
						<li>Residence proof</li>
						<li>Income proof</li>
						<li>Bank statements</li>
						<li>Signature verification proof</li>
						<li>Pro-forma Invoice or Rate List</li>
					</ol>
				</p>
				<h4><b>Documents required after the car loan is sanctioned:</b></h4>
				<p>
					Once the loan is sanctioned and before you receive the loan amount, you need to submit the following set of documents:
					<ol style="margin-left:15px;" type="a">
						<li>Loan Agreement duly signed along with RTO set</li>
						<li>Standing Instruction (SI) Request / ECS Form / Post Date Cheques (PDCs). Security Cheques required for SI and ECS</li>
						<li>Margin money receipt</li>
					</ol>
				</p>
				<h4><b>In conclusion:</b></h4>
				<p>
					<ol style="margin-left:15px;" type="a">
						<li>Have a good credit score</li>
						<li>Know your current financial status</li>
						<li>Pay a higher down payment to reduce the total cost incurred on your purchase, in the long run.</li>
						<li>Get car insurance</li>
						<li>Investigate a lot of banks and financial institutions for their processes and rates of interest before making a choice</li>
						<li>Avoid penalties by choosing institutes that allow flexible payment options. This means, you can pay a few months EMIs earlier than due without incurring additional costs.</li>
						<li>When buying a used car, it is a financially viable decision to make the full payment.</li>
					</ol>
				</p>
