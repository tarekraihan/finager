					<h3>Tips For First Time Home Buyers</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					If you are about to buy your first house, make sure you know what you are doing and that you don’t get caught up in a rookie mistake. There are a lot of things 
					to consider when buying a home, both financially and personally. It is not as simple as one may think. Here are a few tips for first time home buyers.
				</p>
				<br/>
				<h4><b>Make Sure The House Is The Size You Want</b></h4>
				<p>
					When visiting homes, look at how many rooms it has, the size of each room, and the home’s layout. Know what you are seeking and do measurements to calculate 
					the appropriate size of each room, particularly your bedroom. Remodeling costs money and you will be living in the house for at least 10 years. So be attentive
					to the size of each room and be satisfied with them for the long term of your residency there. And remember that you will most likely be accumulating more 
					things throughout the years. Be sure you have the space so you do not outgrow your new home.
				</p>
				<br/>
				<h4><b>Know Your Budget And Credit Score </b></h4>
				<p>
					Knowing what your range is should always be in your mind as well. You see a house you like and want, but it’s out of your price range, and yet, you want to say yes.
					Don’t go over the threshold because, economically speaking, it will be affecting your expenses and bills to pay off the mortgage. Then, there is the credit score 
					where it will make or break your ability to buy the house. Make sure your credit is solid because it will get nit-picked by the mortgage underwriter when you seek 
					a loan. Provide the best and most up to date information about your finances so that the bank can determine the availability of a loan.
				</p>
				<br/>
				<h4><b>Find The Right Real Estate Agent </b></h4>
				<p>
					You’ve seen the commercials. “Like a good neighbor, State Farm is there.” They promote their agents as being extremely helpful and knowledgeable. Find a real
					estate agent who is the same and will treat you right while they help you find the perfect home. They know all about the neighborhoods you would look at and 
					will explain how the process works in buying a house. Also, you’re not paying them; commission pays them from the house seller
				</p>
				<br/>
				<h4><b>Commuting From Work  </b></h4>
				<p>
					Look at the area of where you could live and how far it would take between home and your job. Places close to the city tend to be more expensive. 
					Balance price and convenience. For most first-time buyers, they buy their house from the outer fringes of town because it fits in their initial budget.
					It is frugal, which is good, but the down side is the length of commute and how it affects meeting up with friends and family. Plus, there is the dreaded 
					traffic from the highway and the longer your commute is, the more stressful your commute may be. A survey from the National Association of Realtors in 2015 
					found 30 percent of home buyers factored commute in their purchase. So, unless you want to deal with a sixty-minute drive, try to find a place a little bit
					closer so you won’t lose your mind.
				</p>
				<br/>
				<h4><b>Ask A Friend Or Family Member What They Think</b></h4>
				<p>	
					Get a second and third and fourth opinions from people you trust. There may be something you missed that you wouldn’t like and someone else will point that 
					out for you. Find someone who knows about house size and can give you strong opinions, which can confirm or question what you will buy from this house. The 
					same goes for cost of any possible repairs or upgrades you may want to make with the potential home.
				</p>
				<br/>
				<h4><b>Inspect And Appraise </b></h4>
				<p>	
					Just in case, hire someone to look at the house to see if there are any problems that should be fixed before you close the deal on it so you don’t get stuck
					with the bill. Also, find an independent third party who will estimate the value of the house and see if it matches the price of offering, as a common mistake
					with first-time buyers is overspending. Always good to know you’re not paying $10,000 more than what it’s worth. Some mortgage products require an in-depth 
					inspection and appraisal as part of the loan process. Whether they do or not, be sure you have these done.
				</p>
				<br/>
				<h4><b>It’s The Mortgage Rate, Stupid </b></h4>
				<p>
					Homes become more lucrative to buy as the rates go down because of their affordability. It’s all about that. Look at the mortgage rates, but don’t make 
					it the primary reason to buy in the current market.
				</p>
				<p>
					If you are about to buy a home for the first time, congratulations! It is quite the achievement to buy a home instead of renting a home or an apartment in 
					which there is high demand for. There are a lot of things to consider when going through the process. It is tedious and exhausting, but if done right, you 
					will find and buy the right home the first time. Just make sure you follow these simple tips for first time home buyers.
				</p>
