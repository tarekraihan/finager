					Starting from medical expenses, wedding expenses or any other financial need, personal loan seems to be the 1st option available and also the
					most preferred option to all of us. With the ease of applying a personal loan, it has become one of the easiest forms of readily available money.
				</p>
				<h4><b>What is a personal loan?</b></h4>
				<p>
					A personal loan is an unsecured loan given to the borrower to meet financial expenses without keeping any collateral. It means the borrower of a 
					personal loan does not have to submit any guarantee to the lender. All banks and financial institutions provide personal loans.
					Normally the rate of interest rate ranges from 14 – 30% and differs from bank to bank. In additions, the rate of interest also changes
					from customer to customers within the same bank based on the customer’s credit history, transactions summary and deposits with the bank.
				</p><br/>
				<h4><b>Benefits of a personal loan</b></h4>
				<p>
					<b>No security</b><br/>
					This is the reason why personal loan outweighs other forms of loans. Personal loans are given based on the credit worthiness of the borrower.
					Based on the borrower’s income, past payment history banks grant personal loan with the belief that the borrower will pay back the loan. 
					From a borrower perspective, it is the most convenient loan available without collaterals.

				</p><br/>
				
				<p>
					<b>Repayment options are easy</b><br/>
					A bank loan tenure is from 12 – 60 months. Every month, the borrower has to pay an EMI (Equated Monthly Installment) to the lender. EMI consists of 
					interest and principal. All of the banks provide pre-payment facility where a borrower has the option of pre-closing the loan with a 
					certain loan pre-closure percentage.
				</p>
				<br/>
				<p>
					<b>Easy documentation</b><br/>
					Personal loan require minimum documentation. Usually banks require a proof of identity, income proof and residence proof of the borrower.
				</p><br/>
				<p>
					<b>Low – High amount loans</b><br/>
					Normally bank grants home loan in the range of 20,000 to 20 Lakhs based on the income level of the borrower. In case of urgent fund requirement, personal loan is an ideal option.
				</p><br/>
				<h4><b>Drawbacks of a personal loan</b></h4>
				<p>
					<b>High interest rate</b><br/>
					As personal loans are unsecured, banks charges extremely higher rate of interest to the borrowers. Rate of interest varies from banks to banks and 
					from individuals to individuals. Lack of trans-parency on arriving at the rate of interest makes it unattractive to individuals. 
					The rate of inter¬est varies from 14 – 30 %.
				</p>
				<br/>
				<p>
					<b>Partial payments</b><br/>
					Except few banks, a major¬ity of the banks does not provide the option of partial payments. Even if the borrower is in a position to pay the outstanding
					loan amount, he/ she are not allowed to pay . Pre-payment of the loans are available with a penalty of 2–6% of the outstanding loan amount.
				</p>
