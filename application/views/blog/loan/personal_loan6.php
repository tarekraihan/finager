					<h3>Does It Make Sense to Take a Personal Loan?</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div>
				<p>
					In an ideal world, your monthly cash flow would cover all your expenses — both expected and unexpected — and enable you to reach your financial goals.
					But financial situations are rarely ever that simple and straightforward.
				</p>
				<p>
					What happens if something comes up in your life and your monthly budget nor your cash savings can handle the expense? There’s always the option to borrow,
					and while being in debt isn’t ideal, there are situations where it may make sense.
				</p>
				<p>
					Before you take out your credit card and rack up a balance, look into other options. A personal loan might be a better financial bet.
				</p>
				<br/>
				<h4><b>What Is a Personal Loan (and Why Get One)?</b></h4>
				<p>	
					A personal loan is a type of unsecured loan. “Unsecured” means you don’t put up collateral against the loan. When you take out a personal loan,
					you’ll typically receive the amount borrowed in a lump sum with fixed payment terms and a set interest rate.
				</p>
				<p>	
					Personal loans may be better options than credit cards because they offer better interest rates. Costing you less can be the biggest benefit,
					but a personal loan is also a different kind of credit account than a credit card. Managing various types of credit is one small action you can 
					take to improve your credit score.
				</p>
				<p>	
					Keep in mind this is only true if you manage accounts and loans wisely. Here’s how to do so.
				</p>
				<br/>
				<h4><b>How to Manage Your Personal Loan Responsibly</b></h4>
				<p>
					Again, in an ideal world, you wouldn’t need to borrow money or wait a very long period of time to save up to buy what you want. But in real life, things happen
					and timelines shift. Taking out a personal loan can be an option. You just need to plan and act responsibly with the sum you borrow.
				</p>
				<p>
					Don’t request more than you can reasonably afford to repay — and don’t take out a loan for a greater amount that what you truly need the money for.
					Not only do you need to pay that money back, but you’ll need to pay loan origination fees and whatever the interest rate on, making this option more
					expensive in the long run than simply using cash.
				</p>
				<p>
					Create a repayment plan and stick to it. Know how much you need to allocate toward repaying your personal loan each month, and make it a priority in your budget.
					You may need to cut back on some discretionary spending, like meals out and shopping trips, in order to knock that loan out on time.
				</p>
				<p>
					And before you take out any loan, make sure you fully understand the terms. Understand all the fees associated with the loan, and ask the lender if there are penalties for repaying the loan early.
				</p>
				<br/>
				
				<h4><b>What About Consolidating Debt with a Personal Loan?</b></h4>
				<p>
					Remember how it may make sense to take a personal loan over racking up credit card debt, thanks to a potentially lower interest rate? If you already have credit
					card debt across multiple cards, it may make sense to consolidate that debt with a personal loan.
				</p>
				<p>
					This might be beneficial if you can get a lower interest rate on the personal loan than what you’re paying on your credit cards, and if you could afford the
					monthly repayment on the personal loan.
				</p>
				<p>
					Like most other financial products, personal loans can be useful tools — but only if you wield them wisely and responsibly.
				</p>
