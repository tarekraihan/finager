					<h3>What International Student Loan is Right for Me? </h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					When choosing an international student loan, there are several questions you need to take into consideration. These questions include:
				</p>
				<p>
					Now, before jumping into the loan jargon here are some basic questions you should ask yourself in order to compare loans efficiently:
					<ul style="margin-left:15px;">
						<li>How much money can you get to pay for college costs and/or living expenses?</li>
						<li>How much are the monthly payments?</li>
						<li>When do the payments start and when do they end?</li>
						<li>What is the total cost of the loan—meaning the total payments over the life of the loan?</li>
						<li>Who is responsible for paying back the loan?</li>
					</ul>
				</p>
				<br/>
				<h4><b>Cost of the Loan</b></h4>
				<p>
					The cost of a loan depends on several factors. Of these factors, the interest rate has the biggest impact on loan costs. Generally, students prefer loans
					with the lowest after-tax interest rate.
					<ul style="margin-left:25px;" type="circle">
						<li><b>Interest Rate :</b>Is it variable or fixed? If it is variable, how frequently does it change and is there a cap? Remember that not everyone qualified for the best advertised rate.</li>
						<li><b>Fees :</b>Origination, disbursement, and guarantee/default fees are basically a form of upfront interest. A good general rule of thumb is that every 4% in
						fees is the equivalent of about a 1% increase in the interest rate on a 10-year loan and about a 0.5% increase in the interest rate on a 30-year loan. Additionally,
						some private student loans charge a second fee when the loan enters repayment. Most loans also charge late fees and collection charges (for loans in default).</li>
						<li><b>Interest Capitalization  :</b>students can defer repaying unsubsidized and private student loans during the in-school, grace, and other deferment periods 
						by capitalizing the interest (adding it to the loan balance). The frequency with which capitalization occurs can affect the cost; monthly capitalization is the
						most expensive, and one-time capitalization is the least.</li>
						<li><b>Discounts  :</b>Some lenders offer interest rate reductions or one-time rebates that can reduce the cost of the loan. Some discounts require you to agree 
						to have monthly payments automatically debited from your bank account and receive monthly statements by email; others require you to make all payments on time;
						some are provided when you graduate; and some lenders provide slightly lower interest rates for borrowers who make payments during the in-school payments, choose 
						a shorter repayment term, or have a co-signer.</li>
					</ul>
				</p>
				<br/>
				<h4><b>Eligibility, Flexibility, and Convenience</b></h4>
				<p>
					Other criteria to bear in mind when choosing an international student loan include:
					<ul style="margin-left:25px;" type="circle">
						<li><b>Borrower Eligibility : </b> Is the loan eligibility based on satisfying credit underwriting criteria or is everybody eligible? Most student loans require that the student be enrolled on at least a half-time basis.</li>
						<li><b>Eligible Expenses : </b>While some loans are limited to institutional charges such as tuition and fees, others include living expenses such as room and board, books, and personal expenses or the full cost of attendance.</li>
						<li><b>Loan limits : </b>What are the annual and aggregate (cumulative) loan limits?</li>
						<li><b>Deferment  : </b>Does the loan allow you to defer repayment during your in-school period or does it require that repayment begin immediately? Additionally, 
						is there a grace period after graduation before repayment begins, and if so, how long is the grace period?</li>
						<li><b>Repayment Plans  : </b>Does the lender offer a variety of flexible repayment plans? What about income-based repayment? How often can you change repayment 
						plans? How long is the repayment term? Is the monthly loan payment affordable?</li>
						<li><b>Financial Difficulty  : </b>Does the lender offer forbearances (temporary suspensions of the obligation to repay) if you lose your job or encounter 
						financial difficulty? If so, is the forbearance full or partial? What are the limits on the duration of forbearances? Does the lender charge a fee for each 
						forbearance?</li>
						<li><b>Cancellation  : </b>Federal education loans and some private student loans will discharge the debt if you die or become permanently disabled. Federal loans also offer public service loan forgiveness.</li>
						<li><b>Customer Service  : </b>Can you reach the lender in the evening and on weekends, or are customer service hours limited? Will you have to spend hours on
						hold to reach someone? Does the lender provide an online loan application and a self-service online account management tool? How well does the lender resolve
						problems?</li>
					</ul>
					As you can see, there are a number of factors to take into consideration when choosing a student loan. Make sure you take the time to give the decision the thought it needs.
				</p>
