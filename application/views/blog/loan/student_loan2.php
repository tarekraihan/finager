					<h3>How to Compare Student Loan Options </h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Have you already checked out our International Student Loan Comparison Tool, but realized that you weren’t exactly sure how to compare the results given?
					Deciding which loan is the right one for you can feel a little overwhelming, but here is a cheat sheet on how to compare your different loan options and 
					choose the best one!
				</p>
				<p>
					Now, before jumping into the loan jargon here are some basic questions you should ask yourself in order to compare loans efficiently:
					<ul style="margin-left:15px;">
						<li>Do I plan to work and study at the same time?</li>
						<li>Do I want to get a Master’s?</li>
						<li>Do I want to make loan payments while I am studying or begin repayment once I have graduated?</li>
					</ul>
				</p>
				<br/>
				<h4><b>1. Interest Rates: Variable or Fixed?</b></h4>
				<p>
					Interest rates are the part of taking out a loan that everyone hates, so if you can get the best deal you’ll feel very satisfied. When you apply for a loan, 
					you are asked if you want a variable or fixed interest rate and the distinction is very important.
					<ul style="margin-left:25px;" type="circle">
						<li><b>Variable:</b> A variable rate means exactly that, the interest rate on your loan varies and changes over time. This is considered somewhat of a gamble because if the economy changes, your interest rate can either decrease or increase.</li>
						<li><b>Fixed:</b>Most people automatically go with a fixed interest rate because its stable and, therefore, stays the same as long as your loan exists.
						A fixed rate makes it easier for you to calculate your monthly payments, which in turn allows you to organize your finances. In essence, it gives you more 
						control from day 1.</li>
					</ul>
				</p>
				<br/>
				<h4><b>2.	Repayment Schedule</b></h4>
				<p>
					When choosing the details of your repayment, is when the previous questions could come in handy. It is important to know if you have the option to defer (postpone)
					payments until after you have finished your studies or if you have to begin repayment as soon as you sign your name. Here are 3 of the most common repayment
					schedules and what they mean:
					<ul style="margin-left:25px;" type="circle">
						<li><b>Standard Repayment: </b> Standard repayment usually gives you a 10-year limit to pay off your loan. During those 10 years you have a fixed monthly amount, and the lender usually requires a minimum monthly payment.</li>
						<li><b>Extended Repayment: </b>Extended repayment is similar to standard repayment, in that you have fixed monthly payments. However, you have the option to pay 
						back your loan between 12-30 years. This of course lowers your monthly payments, but it is important to remember that the lender will be charging you interest
						during this time. Although you may have lower monthly payments, more payments means more interest which means more money paid over time.</li>
						<li><b>Graduated Repayment: With a graduated repayment schedule, the idea is that you start off paying a specific monthly payment and that this amount is
						readjusted (increases) every 2 years. The repayment timeline given is usually 12-30 years.</b></li>
					</ul>
				</p>
				<br/>
				<h4><b>3.	Special Conditions and Additional Fees</b></h4>
				<p>
					You may have heard the term deferment thrown around when talking about loans and you may know that it means postponing your loan payments. When taking out a 
					private student loan you must be careful with the fine print. Although deferment exists, with private student loans it might be a little bit trickier. Many times, 
					in the fine print is where you will find a multitude of hidden fees that can come back to haunt you later. Here are a few fees to keep an eye out for:
					<ul style="margin-left:15px;" type="circle">
						<li>Deferment</li>
						<li>Paying off your loan early</li>
						<li>Penalties for late payments</li>
					</ul>
				</p>
