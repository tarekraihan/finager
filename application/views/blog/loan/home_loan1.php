					<h3>Nearly half of mortgage borrowers don’t shop around when they buy a home</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div>
				<p>
					Buying a home is a big purchase, but it’s just that: a purchase. When it comes to spending money on our daily expenses, we have lots of options to help us 
					find the best deal possible. Take, for example, digital gadgets. To get a good deal you can search for sales, find coupon codes, and research whether it’s
					less expensive to buy something from a big box retailer or on the manufacturer’s website.
				</p>
				<p>
					We shop to find the best price for laptops or appliances, but a report of recent mortgage borrowers found that almost half of us don’t shop around for a mortgage when we buy a home.
				</p>
				<p>
					Failing to shop for a mortgage could cost you. Consumers who consider interest rates offered by multiple lenders or brokers may see substantial differences in the
					rates. For example, our research showed that a borrower taking out a 30-year fixed rate conventional loan could get rates that vary by more than half a percent.
					Getting an interest rate of 4.0% instead of 4.5% translates into approximately $60 savings per month. Over the first five years, you would save about $3,500 
					in mortgage payments. In addition, the lower interest rate means that you’d pay off an additional $1,400 in principal in the first five years, even while making 
					lower payments.
				</p>
				<br/>
				<p>The survey of 2013 mortgage borrowers also found that modern mortgage borrowers:</p>
				<h4><b>Often fail to shop:</b></h4>
				<p>
					Almost half of borrowers seriously consider only a single lender or broker before deciding where to apply.
				</p>
				<br/>
				<h4><b>Apply to only one lender or broker:</b></h4>
				<p>	
					Seventy-seven percent of borrowers only end up applying with a single lender or broker, instead of filling out applications with multiple lenders 
					or brokers to see which can offer the best deal.
				</p><br/>
				<h4><b>Rely on information from people with something to sell:</b></h4>
				<p>	
					Lenders and brokers were the most popular source for information about mortgages, with 70 percent of mortgage borrowers reporting that they used them “a lot” as
					a source of information. Thirty percent of borrowers say they relied heavily on their real estate agent for mortgage information. While lenders, brokers,
					and real estate agents can be informative, they also have a stake in the transaction. The report found many fewer, 20 to 41 percent of borrowers, get a lot
					of their information from outside sources such as websites, financial and housing counselors, or friends, relatives or coworkers.
				</p><br/>
				<h4><b>Shop more if they know more: </b></h4>
				<p>	
					Borrowers who were confident about their knowledge of available interest rates were almost twice as likely to shop as consumers who reported being unfamiliar with available interest rates.
				</p>
				<p>
					We believe that mortgage borrowers should be shopping around. We’ve created Owning a Home, an interactive, online set of resources and tools designed to help borrowers approach the mortgage shopping process, with more information.
				</p>
				<p>	
					Owning a Home sets out to help you feel comfortable shopping in the mortgage market. These unbiased tools and resources aim to inform and empower you when you are 
					shopping for a mortgage. The tools take you from the very start of the home buying process through to the closing table. At every step, Owning a Home provides information and questions to ask. The tools include:
				</p>
				<br/>
				<h4><b>Guide to loan options:</b></h4>
				<p>
					A primer on the loans available to help you finance your home. In this resource, you’ll find information on the length or term of loans, whether a loan is fixed
					or adjustable, the different loan types available, including FHA and VA loans.
				</p><br/>
				<h4><b>Tool to see what interest rates are offered to people in your situation:</b></h4>
				<p>
					A dynamic tool that lets you input your information, like credit score and down payment, to see what interest rates people with similar financial situations 
					have been offered. You can play around to see how different factors affect the rates.
				</p><br/>
				<h4><b>Guide to closing documents:</b></h4>
				<p>
					A document that explains the important closing forms, so you know what information is on the forms, and where to find it.
				</p><br/>
				<h4><b>Closing checklist: </b></h4>
				<p>
					Closing is when you finalize your mortgage. You need to go into the process prepared and ready to enter into your contract. Our checklist helps you to realize what you need to do, and gets you ready for closing, one step at a time.
					Our survey will continue each year, and we look forward to hearing more from borrowers to see if our Owning a Home tools change the way the modern mortgage borrower approaches the mortgage market.

				</p>
				<p>
					Check out our report for more in-depth research on consumers’ responses. Then check out Owning a Home to help you navigate the market, ask the questions, and take the steps that will help you find the mortgage that’s right for you.
				</p>
