					<h3>How To Buy A Home Without Credit</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<p>
					Having bad or no credit can be a huge obstacle in many situations, perhaps none more difficult than when you’re trying to buy a home. In fact, homeownership can be
					one of the biggest reasons people set out to build good credit. But, while establishing a strong credit history is the most common route to homeownership, 
					it’s not the only way there. Here we’ll show you a few options on how to buy a home without credit.
				</p>
				<p>
					Like anything else, you can buy a home with cash. In 2015, all-cash transactions made up 30.1% of single-family home and condo purchases (down from a 
					peak of 36.3% in 2012), according to real estate data company RealtyTrac. People may have many reasons for buying a home with cash, but if you have bad or no
					credit, it’s probably your only option.
				</p>
				<br/>
				<h4><b>Pros and cons of paying cash</b></h4>
				<p>
					“Some people have terrible credit and they’re afraid they won’t qualify for a mortgage,” says Diane Saatchi, a senior broker with Saunders & Associates, 
					a real estate company in the Hamptons, on Long Island, NY. Then there are the cash buyers who don’t want to deal with the credit world. “They don’t want 
					to be subjected to the scrutiny of a bank. … They like to live under the radar.”
				</p>
				<p>
					Even if you live in a very inexpensive part of the country, buying a home with cash requires saving a lot of money — or making a lot of money.
				</p>
				<p>
					“If you’re paying $100,000 cash, you’ve either saved for a very long time or you’re coming from some money,” says Doris Phillips, chief operating officer of 
					Lake Homes Realty and a real estate broker in Alabama and Georgia. Oftentimes, people who buy homes with cash are purchasing luxury or secondary homes, she says.
				</p>
				<p>
					Beyond the fact that buying a home with cash doesn’t require you to have good (or any) credit, it can save time and money.
				</p>
				<p>
					“A lot of times, with cash deals there’s only one Realtor involved, so you can really negotiate down the commission,” Phillips says.
				</p>
				<p>
					Closing costs tend to be much lower, as well.
				</p>
				<p>
					“You don’t have to have mortgage insurance, you’re not paying mortgage tax, you’re not paying lender’s costs and underwriter’s costs,” Phillips says. “You’re not paying points to the lender.”
				</p>
				<p>
					She says a cash purchase could take about five days from start to finish, rather than the several weeks that a mortgage requires. As far as logistics go, you sign a 
					handful of documents and wire the money from your bank account to the title company.
				</p>
				<br/>
				<h4><b>Cash-only offers trump all? </b></h4>
				<p>
					Cash purchases certainly have disadvantages, though. You can get a tax deduction for paying mortgage interest, and interest rates are very low right now, so borrowing
					is relatively cheap and allows you to do something else with all that cash. And cash offers aren’t always going to win a bidding war. It really depends on the seller.
				</p>
				<p>
					“We go up against cash buyers all the time,” says Scott Sheldon, a senior loan officer with Sonoma County Mortgages in California. “And a common theme with those 
					people is that [they think] they’re better than everybody. They tend to be very low-ball-oriented offers that we tend to beat out regularly.”
				</p>
				<p>
					On the other hand, it’s low risk for the seller.
				</p>
				<p>
					“A cash deal is pretty much a sure deal,” Phillips says. “Mortgages can fall through the crack with one simple thing.”
				</p>
				<br/>
				<h4><b>Find The Right Real Estate Agent </b></h4>
				<p>
					You’ve seen the commercials. “Like a good neighbor, State Farm is there.” They promote their agents as being extremely helpful and knowledgeable. Find a real
					estate agent who is the same and will treat you right while they help you find the perfect home. They know all about the neighborhoods you would look at and 
					will explain how the process works in buying a house. Also, you’re not paying them; commission pays them from the house seller
				</p>
				<br/>
				<h4><b>Get Pre Approved</b></h4>
				<p>
					If you’re not in a position to buy property with cash but still want to be a homeowner, you can focus on building a good credit score while saving for the purchase. 
					Smart credit use, like making payments on time and spending very little of the available credit on your credit cards, will help you improve your credit scores over 
					time. You can learn more about fixing your credit here and track your progress by getting two of your credit scores for free every month on Credit.com. And check your 
					free credit report every year at AnnualCreditReport.com.
				</p>
