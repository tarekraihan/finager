				<div class="details_heading">
					<h3>Questions to Ask When Leasing a New Car</h3>
					<h4><i>Writen by Sujat, 02-Oct-2016</i></h4>
				</div>
				<p><span class="uper_text">L</span>easing a new car is a serious financial commitment that should not be undergone without carefully
				weighing the pros and cons. While some people advise against leasing a car,
				it is a reasonable and preferable option in many cases. However, 
				there are several questions that one should consider before signing on the dotted line.
				</p><br>
				<h4><b>How much will I owe at the signing of the lease?</b></h4>
				<p>
					Many lease options come with down payments toward the value of the lease that lowers your monthly payments. 
					If you are planning on leasing a car then you should carefully consider whether or not the down payment is worth it.
					Additionally, a large down payment may be lost if your leased car is totaled in an accident or stolen. 
					While your auto insurance will most likely cover the remainder of the lease, your downpayment will be completely lost if this unfortunate situation should occur.
				</p><br>
				<h4><b>How much are owed in taxes at the time of leasing? </b></h4>
				<p>
					In some states, you will owe taxes on the purchase price of the car at the time of leasing.
					Currently the following states require that you pay taxes on the purchase price of the car at 
					the time of the lease signing: Illinois, Texas, Oklahoma, Maryland, Delaware, Arkansas, and Virginia.
					On top of this, most states require you to pay taxes on your lease payments. Additionally, some cities
					or counties will require you to pay additional taxes on your lease payments. If you live in an area known for high taxes then 
					this could add significantly to the overall cost to lease a car.
				</p><br>
				<h4><b>How many miles are allowed and what are the penalties for going over?</b></h4>
				<p>
					Most leases have milage restrictions written into the lease. If you are a road warrior then leasing may not be for you as the most
					common mileage restriction is 12,000 miles per year, or 36,000 miles over the entirety of the lease. Occasionally the mileage restriction can
					be negotiated during the leasing process and in some instances customers are permitted to buy additional miles at the time of the lease signing. 
					The penalties for going over the allotted milage can be steep so it is important to be aware of your average annual mileage when considering if 
					leasing is the right option.
				</p><br>
				<h4><b>What type of auto insurance am I required to carry as per the lease terms?</b></h4>
				<p>
					While most, if not all, states require drivers to carry some form of auto insurance, when you are leasing a car most 
					leases require the lessee carry comprehensive auto insurance. Usually the insurance requirements are as follows: liability – $100,000 per person / $300,000
					per occurrence, property – $50,000, comprehensive and collision – actual value with maximum $500 deductible. If you are used to carrying 
					the minimum insurance requirements this could significantly increase your insurance costs, especially if you have a less than stellar driving record.
				</p><br>
				<h4><b>What will the buy price be at the end of the lease and how can I buy the car?</b></h4>
				<p>
					During the lease negotiation the worth of the car at the end of the lease is set as part of the lease. 
					As the lessee you usually have the option to buy the car at the end of the lease, so be sure to pay attention to this part of 
					the lease negotiation. Usually you must give the dealership at least a month’s notice that you intend to purchase the vehicle 
					prior to the end of the lease. The buy price listed in the lease is often negotiable at the end of the lease based on the value of 
					the car at that given time. Additionally, buying your leased vehicle is an attractive option if you are facing excessive mileage or 
					wear and tear fees at the end of your lease.
				</p><br>
				<h4><b>Am I permitted to transfer the lease and how can I get out of the lease?</b></h4>
				<p>
					Sometimes the unexpected happens and you find yourself either unable to afford the lease payments or unable to drive your vehicle. 
					Should the unexpected happen and you need to end your lease, there are some options. At the time of the lease signing,
					you should be aware of the dealership penalties surrounding ending a lease early. Many lessees choice to transfer the 
					lease rather than end the lease. There are many lease transfer websites that, for a fee, will help you transfer for you lease to another buyer.
					If you are looking to transfer your lease then it is important that your car is in good condition and you are within the mileage 
					restrictions outlined in your lease agreement. If not you will likely have difficulty finding someone to take over your lease and 
					take on the significant fees and penalties along with it.
				</p><br>
				<h4><b>How can I avoid unexpected charges at the end of a lease?</b></h4>
				<p>
					When your lease ends and you return your car to the dealership be sure to read over your lease to know exactly what fees you may
					face when turning in your vehicle. As mentioned previously, the fees for excessive wear and tear and going over your milage can 
					easily be avoided by staying within your allotted mileage and maintaining your vehicle. However, there are other ways to avoid fees.
					Edmunds.com suggest that you detail your car yourself, service your car regularly and keep all maintenance records. Additionally, 
					be sure to check your tires before turning in your vehicle. If you turn in a car with worn or mismatched tires then you may be
					required to pay for the cost of a new set of tires.
				</p>
				<p>
					As a final note, beware of the disposal fee. Some dealerships charge a fee simply for giving the car back to them at the end of the lease. 
					If you are aware of this at the time of the lease negotiation, you may be able to negotiate that fee down, otherwise you may be stuck with it.
				</p>
