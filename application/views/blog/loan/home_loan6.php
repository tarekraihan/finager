				</p>
				<p>
					Why would you need a coach to help you navigate this process? 
				</p>
				<p>
					For the same reason you might use a  job counselor to help you update your resume. We can give you tips along the way and keep you on track. After all, 
					taking on a mortgage is one of the biggest financial commitments most people make in their lifetime. That’s why, if you’re one of the millions of people
					who will take out a mortgage to buy a home this year, we’re here to help. 
				</p>
				<p>
					Best of all, this information, which we hope you find useful, is free!
				</p>
				<p>
					Keep reading to:
					<ul style="margin-left:15px;">
						<li>Understand your rights as a mortgage borrower</li>
						<li>Learn why you should shop for a mortgage, not just “get” one</li>
						<li>Sign up to have this information delivered to your inbox</li>
						<li>Get a roadmap to help you navigate key milestones towards owning a home</li>
					</ul>
				</p>
				<br/>
				<h4><b>You’re in control</b></h4>
				<p>
					Buying a home and taking out a mortgage can feel overwhelming at times. It’s a big commitment, but it’s yours: your decision, your process, and your money.
					You have the right to be in control. 
				</p>
				<p>
					This doesn’t mean you can create your own mortgage, but you can choose what loan offer you accept and which mortgage professionals to work with.
					You’re in the driver’s seat and have every right to stop and ask questions if you’re unsure of a term or not happy with a loan offer. 
				</p>
				<br/>
				<b>Here are three things to remember during your journey to becoming a homeowner: </b>
				<h4><b>You have the right to decide how much you can afford. </b></h4>
				<p>
					A lender can tell you how much they are willing to lend to you, but only you can decide how much you are comfortable spending on a home or monthly payment. 
					When lenders preapprove you for a loan, they don’t see your entire financial picture. A lender may preapprove a loan amount and home price that is more 
					than you feel comfortable spending. If this happens, you should still shop for homes based on the home price you feel comfortable with. A lender’s preapproval
					represents the maximum that they are willing to lend to you – you can always spend less. Only you know all of your other expenses. 
				</p>
				<br/>
				<h4><b>You have the right to understand your mortgage choices. </b></h4>
				<p>
					Mortgages can be complex, but you have the right to clearly understand what you're going to pay upfront and each month, how long you have to pay off your mortgage, 
					and when your payments are due. And you have a right to understand the pros and cons of the different choices available to you. Ask questions until you understand.
					If someone you’re working with can’t or won’t answer your questions, consider finding someone else to work with. Don’t accept a loan offer that you don’t understand 
					or aren’t comfortable with.  
				</p>
				<br/>
				<h4><b>You have the right to shop for your mortgage.</b></h4>
				<p>
					You’re probably not planning to buy the first home you see without doing some comparison shopping. Approach your mortgage search the same way. Seek out mortgage 
					offers from at least three lenders to help you understand your options. It can give you peace of mind that you know what a good deal is, and give you leverage to
					negotiate a deal that works for you. A difference of half a percent in the interest rate on your mortgage can save you thousands of dollars in the long run. 
				</p>
				<br/>
				<h4><b>Let’s get started</b></h4>
				<p>
