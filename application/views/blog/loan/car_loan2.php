					<h3>5 Reasons to Have Your Car Loan Ready Before Buying</h3>
					<h4><i>Writen by Sujat, 02-Oct-2016</i></h4>
				</div>
				<p><span class="uper_text">W</span>hy go through the trouble of getting a car loan from a bank or credit union, 
					rather than just getting it through the dealership? It’s certainly easier to let the dealership handle the financing for you,
					but by doing so you may be conceding too much. Here are five reasons to have your car loan ready before buying. 
				</p><br>
				<h4><b>1. It Gives You a Stronger Bargaining Position</b></h4>
				<p>
					It’s all too easy to “throw yourself on the mercy of the dealer” for the entire purchase transaction. 
					After all, car dealerships routinely handle virtually all aspects of car purchase, including:
					<ul style="margin-left:12px;">
						<li>Buying your current car as a trade-in</li>
						<li>Providing down payment assistance (a.k.a., cashback arrangements)</li>
						<li>Providing the financing</li>
						<li>And of course, supplying the car you will buy</li>
					</ul>
				</p>
				<p>
					That may be a convenience, but it also gives the dealer way too much power. The more of those steps that you can handle on your own, 
					the weaker the dealership will be, and the stronger you will be. This is especially important when it comes to financing. 
				</p>
				
				<p>
					When you have your loan already in place walking in the door, your bargaining position is immediately stronger. 
					It shows that you aren’t totally reliant on the dealer, but just as important, it’s validation that your status 
					as a qualified buyer has been reviewed and approved by a lending institution.
				</p><br>
				<h4><b>2. You’ll Have More Confidence Going into the Deal</b></h4>
				<p>
					There’s a huge psychological advantage to having been preapproved by your own lender before walking into a dealership.
					You will have the confidence of knowing that you are fully qualified, and you don’t have to rely on the dealer to make that decision.
				</p>
				<p>
					That confidence will show in all aspects of the car buying process. 
					The dealer will be forced to respect it. After all, since you already have your financing lined up, 
					you can walk out the door and buy a car from any other dealership in town.
				</p>
				<br>
				<h4><b>3. It Forces the Dealer to Give You a Better Financing Deal</b></h4>
				<p>
					There may have been a time, very long ago, when the primary reason why car dealerships offered financing was 
					to ensure the sale of their cars. But today, financing is an important part of car dealerships profit on any given sale. 
					If the dealer’s handling the financing, they’ll not only make money on the sale of the car, but also on the loan.
				</p>
				<p>
					For that reason, the dealer will almost certainly make you a financing offer even though you have your own loan waiting in the wings.
					That also means that the dealer will be forced to give you a better deal than the one you have on your own.
				</p>
				<p>
					This is doubly important because it virtually eliminates the possibility that the dealer might put you into
					a high priced loan that will increase their own profit, while costing you more money every month.
				</p><br>
				<h4><b>4. You’ll Have a Chance to Clean Up Your Credit If Needed</b></h4>
				<p>
					Applying for a car loan before you actually go into a dealership gives you an advanced opportunity to make sure that you 
					are in fact qualified to get the best terms available. And if you’re not — if there are certain credit issues — you will have 
					an opportunity to fix them so that you can get a better deal.
				</p>
				<p>
					This is not something you ever want to find out for the first time when you’re sitting in a car dealership and have already 
					chosen a car and become emotionally attached it. At that moment, you might go ahead and complete the transaction - high rate 
					loan and all - because you have passed the point of no return on the purchase.
				</p><br>
				<h4><b>5. It’s One Less Party Having Access to Your Identity</b></h4>
				<p>
					This isn’t an issue that most people think about when getting a car loan, but it shouldn’t be ignored either. 
					The more people who have access to your identity, the greater the chance that your information could end up in the wrong hands.
					That’s a recipe for identity theft.
				</p>
				<p>
					Making the possibility even more likely is the fact that dealerships often have financing arrangements through third-party sources. 
					That means that your personal financial information will not only be sitting somewhere in the dealership, 
					but it will also go out to a third-party lender. That’s at least two open doors for identity theft.
					More if the dealership shops your application through multiple lending sources.
				</p>
				<p>
					By contrast, when you apply at a bank, the loan will be made by the bank itself, so your information won’t be shopped around.
					And if you obtain a loan from a bank or credit union that you already have a business relationship with,
					they will already have your information on file, and they won’t be putting your information in someone else’s hands.
				</p>
				<p>
					When you think about all that you gain by having your car loan ready before buying — especially your improved 
					bargaining power — it’s well worth the advanced legwork to do it on your own.
				</p>
