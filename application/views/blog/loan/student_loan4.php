				<div class="details_heading">
					<h3>THE TOP WAYS TO GET STUDENT LOAN FORGIVENESS</h3>
					<h4><i>Asef Jamil, 02-Oct-2016</i></h4>
				</div><br/>
				<h4><b>THE TOP WAYS TO GET STUDENT LOAN FORGIVENESS</b></h4>
				<p>
					Student loans can be a great investment in your future, or can be a huge burden if not fully thought out or abused. If you currently have a student loan or are
					thinking about getting student loans, you should know that student loans CANNOT discharged in bankruptcy (in most cases).
				</p>
				<p>
					This means that they will stick with you for the rest of your life, unless you pay them off, or, if you are lucky enough, qualify for student loan forgiveness.
					Under certain circumstances, all or part of your student loan can be canceled in a process called Student Loan Forgiveness. To qualify, you must perform volunteer work,
					perform military service, practice medicine in specific communities, or meet other criteria.
				</p>
				<p>
					If you don’t qualify, refinancing your student debt presents an alternate opportunity to save thousands. Credible enables you to fill out one form and look 
					at personalized offers from multiple lenders.
				</p>
				<p>
					The great thing about student loan forgiveness, unlike other debt, is that the amount forgiven is NOT treated as taxable income. Other types of student loan discharge are taxable.
				</p>
				<p>
					Remember, if you want specific help, check out our program Ditch Your Student Loans. Also, the list below is just for Federal student loan forgiveness programs. 
					If you want to see if your State offers any programs, check out this tool: Student Loan Forgiveness by State.
				</p>
				<p>
					Finally, make sure you’re not getting scammed with your student loans, and make sure you’re not overpaying for help with your student loan debt either.
				</p>
				<br/>
				<h4><b>Public Service Loan Forgiveness</b></h4>
				<p>
					The #1 way to currently get student loan forgiveness is to work in public service for 10 years. President Obama announced the PSLF – Public Service Loan 
					Forgiveness Program, which will grant student loan forgiveness on qualifying loans after 120 payments (10 years).
				</p>
				<p>
					The great thing about public service is that the definition is very broad. Qualifying employment is any employment with a federal, state, or local government agency,
					entity, or organization or a not-for-profit organization that has been designated as tax-exempt by the Internal Revenue Service (IRS) under Section 501(c)(3) of the
					Internal Revenue Code (IRC). The type or nature of employment with the organization does not matter for PSLF purposes.
				</p>
				<p>
					For example:
					<ul style="margin-left:15px;">
						<li>Government Workers (Federal, State, Local)</li>
						<li>Emergency management</li>
						<li>Military service</li>
						<li>Public safety or law enforcement services</li>
						<li>Public health services</li>
						<li>Teachers</li>
						<li>Public education or public library services</li>
						<li>School library and other school-based services</li>
						<li>Public interest law services</li>
						<li>Early childhood education</li>
						<li>Public service for individuals with disabilities and the elderly</li>
					</ul>
					<p>The organization must not be a labor union or a partisan political organization.</p>
					<p>If you want step-by-step help filing for Public Service Loan Forgiveness, check out our short 15 minute course below: PSLF Training Program.</p>
				</p><br/>
				<h4><b>Volunteer Work</b></h4>
				<p>
					While some volunteer work can be a huge life commitment, organizations such as VISTA all you to have a semblance of life while volunteering. They also offer
					money to be used towards your student loan debt, which is great!
				</p>
				<p>
					Peace Corps – If you volunteer for the Peace Corps, you get automatic deferment of Stafford, Perkins, or Consolidation loans. You can also get partial cancellation 
					of Perkins Loans based on the number of years of service. Currently, you get 15% per year, with a max of 70%.
				</p>
				<p>
					Americorps – If you serve for 12 months, you can receive $4,725 towards your student loans.
				</p>
				<p>
					Volunteers in Service to America (VISTA) – If you volunteer for 1,700 hours, you can receive $4,725 towards your student loans.
				</p><br/>
				<h4><b>Military</b></h4>
				<p>
					Military student loan forgiveness offered by each branch has been replaced by Public Service Loan Forgiveness.
				</p>
				<p>
					You can currently see the student loan benefits that military personnel are eligible for here.
				</p><br/>
				<h4><b>Teaching</b></h4>
				<p>
					Students who become full-time teachers in an elementary or secondary school that serves low-income families can have a portion of their Perkins Loan forgiven. 
					This program forgives 15% of your loan in each of the first and second years, 20% in each of the third and fourth years, and the remaining 30% in the fifth year.
				</p>
				<p>
					This program will be phased out in lieu of Public Service Loan Forgiveness.
				</p>
				<br/>
				<h4><b>Legal Studies</b></h4>
				<p>
					Many law schools forgive the student loans of students who serve in the public interest or for non-profits. These loan repayment assistance programs provide
					loan repayment or forgiveness, lower interest rates on loans, or postponed payment schedules. Most programs have income limitations and specify which types 
					of employment qualify.
				</p>
				<p>
					Furthermore, many 23 states offer loan repayment assistance programs: Arizona, District of Columbia, Florida, Illinois, Indiana, Iowa, Louisiana, Maine, Maryland,
					Massachusetts, Minnesota, Montana, New Hampshire, New Mexico, New York (two programs), North Carolina, Ohio, Oregon, South Carolina, Texas, Vermont, and Virginia.
				</p>
				<p>
					For more information, check out the American Bar Association’s website on Loan Repayment Assistance Programs. You can also learn more about this on our Student Loan Forgiveness by State.
				</p>
				<br/>
				<h4><b>Medical Studies</b></h4>
				<p>
					If you are a medical student, there are several options available for student loan forgiveness.  While a career in health care and other medical services is always 
					in demand, everyone knows that tuition for school can get very expensive, even if you are taking online courses. The thought of the burden caused from student loans 
					can be daunting when considering pursuing a career in nursing. But forgiveness programs like the ones below offer some peace of mind knowing that you can go after 
					that Master’s degree in nursing and help those in need of medical care without the fear of what is to come after graduation.
				</p>
				<p>
					The U.S. Department of Health and Human Services offers loan forgiveness programs through two programs:
				</p>
				<p>
					National Health Service Corps: Complete a five-year commitment to providing medical care in “needed” areas, and you can receive up to $170,000 in loan forgiveness.
				</p>
				<p>
					Nursing Education Loan Repayment Program: For two years of nursing service at a qualifying non-profit, you can have 60% of your loan forgiven. There is also an 
					optional third-year, where you can have an additional 25% forgiven.
				</p>
				<p>
					If you are interested in research, the National Institute of Health offers a loan forgiveness program that repays up to $35,000 each year for qualifying research 
					at either a non-profit or university, as well as directly at the National Institute of Health.
				</p>
				<p>
					If you are interested in veterinary medicine, the U.S. Department of Agriculture offers a Veterinary Medicine Loan Repayment Program where you can receive loan 
					forgiveness of $25,000 per year for three years, as long as you work in a designated area.
				</p>
				<p>
					Finally, many individual colleges offer different types of loan forgiveness programs, so check with your local college.
				</p>
				<br/>
				<h4><b>Other Ways To Get Student Loan Forgiveness</b></h4>
				<p>
					Remember, if you have private student loans, there are no ways to get student loan forgiveness. The best bet is to consider refinancing your private student 
					loan to lower your interest rate or change your payment terms to hopefully make your loan more affordable. We recommend using the service Credible, which
					quickly compares your student loan options.
				</p>
