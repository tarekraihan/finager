					<h3>What Is a Debit Card and How Is It Used?</h3>
					<h4><i>Akter ul alam, 02-Oct-2016</i></h4>
				</div>
				<br/>
				<p>
					Debit cards are one of the most basic bank cards, and using them the right way can help you avoid unnecessary costs. Here’s what to know.
				</p><br/>
				<h4><b>What is a debit card?</b></h4>
				<p>
					A debit card, also known as a “check card,” uses money from a linked checking account for purchases. It’s a safe and convenient alternative to using cash or checks,
					and it works for in-person and online purchases. You can also use this card at automated teller machines, or ATMs, when you need to get cash, deposit checks, 
					transfer between accounts or see bank account balances.
				</p><br/>
				<h4><b>Fees</b></h4>
				<p>
					Debit card transactions are not always free. Here are some costs to watch out for:
					<ul style="margin-left:15px;">
						<li><b>Out-of-network ATM fee:</b> This occurs if you use an ATM that isn’t in your bank’s network. Many banks charge $2 to $3 per ATM transaction.</li>
						<li><b>Foreign transaction and ATM fees:</b> These generally occur if you make purchases or use ATMs outside the U.S. International ATM fees are small fixed costs, while foreign transaction fees usually range from 1% to 3% of the purchase.</li>
						<li><b>Debit card replacement fee:</b> If you misplace your card, there’s a small fee to have another one mailed to you. But most banks don’t charge you when they reissue your card after it expires.</li>
						<br/>There’s also one fee that can happen unexpectedly:
						<li><b>Overdraft or non-sufficient funds fee:</b> A bank charges you one of these two fees if you try to buy something but don’t have enough money in your checking account. If you have a form of overdraft protection, your bank will cover the transaction but you’ll owe that amount plus an overdraft fee. If you don’t have coverage, the transaction usually gets declined and you’ll get charged a non-sufficient funds fee.</li>
					</ul>
				</p><br/>
				<h4><b>How secure are debit cards?</b></h4>	
				<p>
					These cards are safer to use than cash or a check, but they have fewer federal fraud protections than credit cards. If your debit card gets lost or stolen, 
					the maximum amount you’d be responsible for depends on when you report the loss or theft to your bank.
					<ul style="margin-left:15px;">
						<li>Before any fraudulent charges occur: $0</li>
						<li>Within two business days: $50 limit</li>
						<li>Within 60 calendar days: $500 limit</li>
						<li>After 60 days: No protection</li>
					</ul>
					So you can be responsible for virtually everything if you don’t report fraud. If just your card number is stolen, however, you’re protected for all
					fraudulent charges for up to 60 days.Credit cards, on the other hand, have a maximum loss of $50 for unauthorized charges, and many issuers offer 
					protection for any card fraud.Debit cards make it easy to get cash and pay for things. Knowing what they are and when to use them will help you avoid 
					fees and other inconveniences.
				</p>				
