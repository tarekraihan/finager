					<h3>Top Ten Credit Cards – January 2017</h3>
					<h4><i>Akter ul alam, 02-Oct-2016</i></h4>
				</div>
				<br>
				<p>
					The following is the January 2017 edition of what we consider the Top 10 credit card products available at the moment.
					The list is based on a combination of factors, including current welcome bonus offers, daily earning potential,
					transfer partners and bonus categories, along with the ability to redeem.
				</p><br/>
				<p>
					<b>1.</b> Chase Sapphire Reserve Pros: Welcome bonus offer of 100,000 Ultimate Rewards points after $4,000 spent in the first three months of account opening.
					Earns 3X points per $1 on travel and dining. $100 credit towards Global Entry or TSA PreCheck application fee every five years.
					50 percent off when you redeem your points through Chase Ultimate Rewards for travel, providing an effective value of 1.5 cents/point.
					Ultimate Rewards points earned transfer on a 1:1 ratio to Chase partners British Airways Executive Club, Singapore KrisFlyer, 
					Korean Air SKYPASS, Southwest Airlines Rapid Rewards, United MileagePlus, Virgin Atlantic Flying Club, Air France/KLM Flying Blue,
					Hyatt Gold Passport, Marriott/Ritz-Carlton Rewards and IHG Rewards Club. No foreign transaction fees. Primary Rental Car Insurance. 
					Priority Pass Select Cons: $4,000 minimum spend requirement to get the welcome bonus. Annual fee of $450 that is not waived the first year.
				</p><br/>
				<p>
					<b>2.</b> Ink Business Preferred Credit Card Pros: Earn 80,000 bonus points after you spend $5,000 on purchases in the first 3 months from account opening.
					That’s $1,000 toward travel when you redeem through Chase Ultimate Rewards. Earn 3 points per $1 on the first $150,000 spent in select categories each
					account anniversary year. Earn 1 point per $1 on all other purchases–with no limit to the amount you can earn. Points are worth 25% more when you redeem
					for travel through Chase Ultimate Rewards. Redeem points for travel, cash back, gift cards and more – your points don’t expire as long as your account is open. 
					No foreign transaction fees. $95 Annual Fee. Ultimate Rewards points earned transfer on a 1:1 ratio to Chase partners British Airways Executive Club,
					Singapore KrisFlyer, Korean Air SKYPASS, Southwest Airlines Rapid Rewards, United MileagePlus, Virgin Atlantic Flying Club, Air France/KLM Flying Blue,
					Hyatt Gold Passport, Marriott/Ritz-Carlton Rewards and IHG Rewards Club. Cons $5,000 minimum spend requirement to get the welcome bonus. 
					Annual fee of $95 that is not waived the first year. As this is a business card, you must have a business to apply for this card. 
				</p><br/>
				<p>
					<b>3.</b>	Chase Sapphire Preferred Card  Pros Welcome bonus offer of 50,000 Ultimate Rewards points after $4,000 spent in the first three months of account opening.
					Additionally, you can earn 5,000 bonus Ultimate Rewards points after adding the first authorized user and that user making a purchase with their card within 
					the first three months from account opening. Earns 2X points on all travel and dining purchases. Chase’s definition of travel is extremely liberal,
					extending from hotels and airlines to taxis, Uber, and parking. 20 percent off when you redeem your points through Chase Ultimate Rewards for travel,
					providing an effective value of 1.25 cents/point. No foreign transaction fees. One of the best looking cards around (made of metal). 
					Ultimate Rewards points earned transfer on a 1:1 ratio to Chase partners British Airways Executive Club, Singapore KrisFlyer, Korean Air SKYPASS,
					Southwest Airlines Rapid Rewards, United MileagePlus, Virgin Atlantic Flying Club, Air France/KLM Flying Blue, Hyatt Gold Passport,
					Marriott/Ritz-Carlton Rewards and IHG Rewards Club. Introductory Annual Fee of $0 the first year, then $95. Cons Higher than average 
					minimum spend requirement. (typically we see $3,000 or less) $95 annual fee (waived the first year), although it is easily justified through its many benefits.
					Almost impossible to receive approval from Chase if you have applied for more than five credit cards from any bank over the last 24 months. 
				</p><br/>
					
				<p>
					<b>4.</b> Citi Prestige Card Pros 40,000 ThankYou Points after $4,000 spent in the first three months of account opening. Earn 3X points on air travel and 
					hotel purchases. Earn 2X points on dining and entertainment purchases. ThankYou Points earned from the Citi Prestige Card can be transferred on a 1:1 ratio 
					to Citi’s transfer partners Cathay Pacific Asia Miles, Garuda Indonesia Frequent Flyer, Singapore Airlines KrisFlyer, EVA Air Infinity MileageLands, Malaysia 
					Airlines Enrich, Thai Airways Royal Orchid Plus, Etihad Etihad Guest, Qantas Frequent Flyer, Virgin America Elevate, Virgin Atlantic Flying Club, 
					Air France/KLM Flying Blue, Qatar Airways Privilege Club and Hilton HHonors. ThankYou Points earned from the Citi Prestige Card can be applied toward
					airfare at 1.6 cents per dollar (60 percent more value) on flights with American Airlines and at 1.33 cents per dollar (33 percent more value) on flights
					with all other airlines. $250 air travel credit each year, which can be used for any airline related purchase including airfare across any airline. 
					Complimentary fourth night for any hotel stay. Complimentary access to over 50 American Airlines Admirals Club lounges, plus hundreds of VIP lounges 
					through Priority Pass Select. $100 Global Entry application fee credit every five years. No foreign transaction fees. Access to the MasterCard Airport
					Concierge service. Cons High annual fee of $450 (not waived the first year), although it is quite easy to mitigate this fee. Less than ideal travel transfer partners. 
				</p><br/>
				
				<p>
					<b>5.</b> Starwood Preferred Guest Business Credit Card from American Express Pros Welcome bonus offer of 25,000 Starpoints after $5,000 are spent in
					purchases within the first three months. Earns 2X Starpoints per $1 spent on the card for each dollar of eligible purchases charged directly 
					with hotels and resorts participating in the Starwood Preferred Guest program. Provides two stays and five nights elite credit each year toward 
					SPG Gold and Platinum Elite status. Starpoints earned can be transferred mostly on a 1:1 ratio to SPG’s transfer partners, which include many of 
					the world’s top airlines. For every 20,000 Starpoints that you transfer to one of SPG’s airline partners, you get an additional 5,000 points or miles bonus,
					which makes this one of the best cards to use for purchases that do not earn additional bonus points. Earn SPG Gold status after spending $30,000 or more in 
					eligible purchases on the card in a calendar year. Provides complimentary unlimited Boingo Wi-Fi and access to Sheraton Club Lounge when staying at Sheraton hotel.
					Eligible for Amex OPEN Savings benefit. No foreign transaction fees. Cons $95 annual fee (waived the first year). No bonus categories outside of Starwood hotel stays.
				</p><br/>
				
				<p>
					<b>6.</b> Starwood Preferred Guest Credit Card from American Express Pros Welcome bonus offer of 25,000 Starpoints after $3,000 are spent in purchases within the
					first three months. Earns 2X Starpoints per $1 spent on the card for each dollar of eligible purchases charged directly with hotels and resorts participating 
					in the Starwood Preferred Guest program. Provides two stays and five nights elite credit each year toward SPG Gold and Platinum Elite status. Starpoints earned 
					can be transferred mostly on a 1:1 ratio to SPG’s transfer partners, which include many of the world’s top airlines. For every 20,000 Starpoints that you transfer
					to one of SPG’s airline partners, you get an additional 5,000 points or miles bonus, which makes this one of the best cards to use for purchases that do not earn
					bonus points. Provides complimentary unlimited Boingo Wi-Fi. No foreign transaction fees. Cons $95 annual fee (waived the first year). No bonus categories outside
					of Starwood hotel stays. 
				</p><br/>
				
				<p>
					<b>7.</b> CitiBusiness / AAdvantage Platinum Select World MasterCard (7) Pros 50,000 Bonus AAdvantage miles after you spend $3,000 within the first three months 
					of card-membership. First checked bag on domestic AA flights is complimentary. Group 1 boarding on domestic AA flights. 
					Spend $30,000 in a calendar year on the card and earn an AA companion certificate. $95 annual fee waived for the first 12 months of card-membership Cons
					This is a business credit card so you must have an eligible business to apply. American’s award chart recently went through a devaluation — check current
					charts to understand updated pricing. 
				</p><br/>
				
				<p>
					<b>8.</b> Spark Cash from Capital One Pros $500 cash bonus after you spend $4,500 within the first three months of card-membership Unlimited 2% cash-back on all purchases Employee cards come at no additional cost $0 introductory annual fee (then $59/year after) No foreign transaction fees Cons This is a business credit card so you must have an eligible business to apply Capital One will pull your credit report from all three major credit reporting agencies (most banks only pull one). 
				</p><br/>
				<p>
					<b>9.</b> Barclaycard Arrival Plus World Elite MasterCard Pros 50,000 bonus “miles” after you spend $3,000 within the first 90 days of card membership. Earn 2x “miles” on 
					all purchases. Covers almost any type of travel-related expense including Airbnb. Issued by Barclays Bank, allowing you to diversify your credit portfolio 
					with another financial institution. No foreign transaction fees. Cons The “miles” that this card earns are not airline miles. They’re effectively valued at 
					one mile equals one penny when redeeming for travel so 10,000 miles converts to $100. Don’t be confused by quality marketing. $89 Annual fee. While it is waived 
					the first year, after the first year of card membership you’re likely better sticking with a 2 percent cash back credit card that has no annual fee such as the Citi 
					Double Cash. 
				</p><br/>
				<p>
					<b>10.</b> The Ritz Carlton Credit Card Pros Three complimentary night at any participating Tier 1-4 Ritz-Carlton hotel, after you spend $5,000 on purchases in the first 
					3 months after account opening. 10,000 bonus points after you add the first authorized user and make a purchase in the first 3 months from account opening.
					$300 annual travel credit to use for baggage fees, seat upgrades, access to your preferred airport lounge and more. Complimentary annual Priority Pass Select
					membership with access to over 900 VIP airport lounges in over 400 cities worldwide (~$399 retail value). $100 Global Entry Application Fee Reimbursement. 
					$100 airline ticket discount on the total cost of between 2 and 5 qualifying round-trip, domestic coach airline tickets purchases on the same itinerary. 
					$100 hotel credit toward dining, spa or other hotel recreational activities on paid stays of two nights or longer. Complimentary Gold Elite status in The
					Ritz-Carlton Rewards program for your first account year. To continue to enjoy Gold Elite Status benefits, simply spend $10,000 your first year and each 
					subsequent year to maintain your Gold Elite status. Platinum Elite status when you spend $75,000 on purchases each account year. Upgrade to The Ritz-Carlton 
					Club level three times annually on paid stays of up to 7 nights. 5 points per $1 spent at participating Ritz-Carlton Rewards and SPG hotels. 2 points per $1 
					spent on airline tickets purchased directly with the airline, and at car rental agencies and restaurants. 1 point per $1 spent on all other card purchases. 
					No foreign transaction fees. Cons $450 annual fee Limited Ritz Carlton portfolio provides fewer opportunities for redeeming the sign-up bonus.
				</p>
