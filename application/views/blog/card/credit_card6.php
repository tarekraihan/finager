					<h3>Refine Your New Year’s Resolutions With These Credit Card Tools</h3>
					<h4><i>Akter ul alam, 02-Oct-2016</i></h4>
				</div>
				<br/>
				<p>
					Here’s the scary thing about financial New Year’s resolutions: Failing is expensive.<br/>
					Botching a pledge to “get credit card spending under control,” for instance, isn’t just a minor goof. It could mire you in high-interest debt
					or hurt your retirement savings. If you really want to get your finances into shape, you first need to get specific about your goals.<br/>
					That’s where your credit card’s online tools can help.
				</p><br/>
				<h4><b>If your resolution is ‘Spend less’</b></h4>
				<p>
					To prevent overspending, zero in on the flabbiest parts of your budget. Many issuers’ sites let you see your spending as a pie chart. You’ll usually find 
					it tucked behind a tab or link titled “Tools” or “Spending Summary.” The chart shows how much you’ve spent in the past year in categories tracked by the issuer,
					such as groceries, gas, restaurants and so on. It also shows your spending in each category as a percentage of your total spending.
				</p><br/>
				<h4><b>How credit card tools can help</b></h4>
				<p>
					Seeing your spending distilled in pie chart form might just make you step back and say, “Whoa, I spent $10,000 at department stores last year?” Realizing weaknesses 
					like these in your spending habits can help you shape a better budget for 2017. <br/>
					Instead of resolving to spend less money, try zero-based budgeting. That is, start with a budget of zero, and add only the expenses you can justify. 
					This approach can help eliminate unnecessary spending altogether, rather than just reduce it.
				</p><br/>
				<b>» MORE:</b> How to build a budget
				<h4><b>If your resolution is ‘Pay down debt’</b></h4>
				<p>	
					The Federal Reserve might raise interest rates three times in 2017, according to Fed projections. That could make your credit card debt more expensive.
					If you’re in the red, make a plan to pay down your balances this year. A good first step is to use your credit cards’ online portals to check your balances 
					and interest rates.
				</p><br/>
				<h4><b>How credit card tools can help</b></h4>	
				<p>
					Your credit card portal will tell you exactly how much you owe and on what terms. To check the annual percentage rate on a card, look for a tab with
					a label like “Limits, Payments and Rates” or “Terms and Agreements.” Or download a monthly statement; the APR will be listed there. Generally, 
					you’ll save more money if you pay down your highest-interest balances down first.
				</p>
				<p>
					You may want to consolidate as much of your debt as possible on a 0% balance transfer APR card, if you can qualify for one. This can help you pay down 
					your balances faster, for less money. If one of your issuers offers a debt pay-down calculator, use it to calculate how long it will take you to become 
					debt-free, based on your current monthly payments.
				</p>
				<br/>
				<b>» MORE:</b> How to pay off debt
				<h4><b>If your resolution is ‘Get organized’</b></h4>
				<p>
					If your pocketbook is overflowing with credit cards you never use, consider New Year’s your opportunity to straighten up. Although it’s generally good 
					idea to keep dormant accounts open because your credit may benefit, it becomes a bad idea when there are annual fees involved. If a card you don’t use 
					charges an annual fee, either convert it to a no-fee account or close it.<br/>
					As long as you’re not about to apply for a mortgage or auto loan, the temporary hit to your credit from closing such cards likely won’t affect you much. 
					Just make sure you’ve logged onto your account and tied up all your loose ends first.
				</p><br/>
				<h4><b>How credit card tools can help</b></h4>
				<p>
					Calling it quits with your plastic usually means forfeiting rewards, such as points, miles or cash back. Before closing an account,
					check your rewards balance to make sure you’re not giving up a small fortune. If you have leftover rewards, redeem what you can.
					If that’s not possible, consider making a donation.<br/>
					Finally, move recurring payments to other cards or cancel them. Until you formally cancel an unwanted subscription, you’re still on the hook for payments.
					Start by sorting through your credit card purchase history to jog your memory about any forgotten subscriptions. 
					After moving or canceling your recurring charges, you’ll be able to close your account with confidence.
				</p><br/>

