					<h3>The Basics of Your Debit Card</h3>
					<h4><i>Akter ul alam, 02-Oct-2016</i></h4>
				</div>
				<br/>
				<p>
					Debit cards are being used at an all-time high today and are used more often than credit cards. Last year debit card use exceeded a trillion dollars. 
					That's a lot of people using debit cards! Although we like to see the increase of debit cards versus credit cards, we still want to make sure you're
					being careful with your debit card.<br/>
					Since a debit card is directly linked to your bank account, it's a convenient way to <b>purchase things without incurring debt</b>. When you use a debit card,
					the money is immediately withdrawn from your account, which means no interest, late fees, over-the-limit fees or annual fees—all good things to help you avoid debt. 
					Debit cards are also great to use because they don't require you to carry cash or write a check.
				</p><br/>
				<h4><b>Know your PIN</b></h4>
				<p>
					When you make a purchase with your debit card, you should have the choice of running it as a debit or credit purchase. Always choose credit. Credit? Yes. 
					This will <b>insure that you are protected by the card company's zero-liability policy</b>—you will not be responsible for unauthorized transactions. 
					If you have to use your PIN, be sure to memorize your PIN and never carry it with you. Report lost cards immediately, change your PIN frequently,
					and use your debit card only if you must.
				</p><br/>
				<h4><b>Check bank statements</b></h4>
				<p>
					To insure that no one but you is using your debit card, check your bank statements online every day. It may seem tedious, but it's better than someone stealing your 
					money. Be sure your internet connection and computer are secure before logging into your personal information. If you spot anything suspicious,
					call your bank immediately.
				</p><br/>
				<h4><b>Watch your debit card</b></h4>	
				<p>
					Keep your eyes on your debit card when transactions are taking place. It should be within your sight at all times. Once the card leaves your view, 
					anyone has access to your card information and ultimately your bank account. That's why it's always best to use cash!
				</p><br/>
				<h4><b>Check your credit report</b></h4>
				<p>
					Every year you can order a free credit report from each of the three nationwide consumer credit reporting companies (one per company so you really get three a year). 
					Do it! If you think someone used your debit card, report it to the credit bureau immediately and request a copy of your credit report. Monitoring it regularly will help cut down on any illegal activities.
				</p>
				<br/>
				<h4><b>Be on the lookout.</b></h4>
				<p>
					The latest scam involves criminals attaching "skimmers" to card-swiping devices and gaining access to your personal information and bank account. 
					If a card-swiper looks questionable, don't use it.<b> Never use an unbranded ATM</b>.
				</p><br/>
				
