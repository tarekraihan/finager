						<li>General rewards cards are better than airline-specific cards. Unless you fly a majority of flights on the SAME airline, I prefer a general travel card instead of an airline-specific card (like a United card). For example, I fly Jetblue and Virgin a lot, so I want a travel card that I can redeem on multiple airlines, not just one.</li>
						<li>Annual fees are not Satan’s spawn. I know it may be blasphemy to personal-finance “experts,” but I’m willing to pay an annual fee for my credit card! OMG! This is why you can call me RTR: Ramit The Rebel. In some cases, there are no-fee versions of the card, so you should always calculate if you spend enough to justify it. Still, $65/year is just not that significant to my financial situation any more.</li>
						<li>I am merciless about using my credit card perks. Credit card benefits can easily be worth $1,000/year. DO THIS.</li>
					</ol>
