					<h3>NerdWallet’s Best Credit Card Tips for January 2017</h3>
					<h4><i>Akter ul alam, 02-Oct-2016</i></h4>
				</div>
				<br/>
				<p>
					January is a time to reflect on the year just passed and to make resolutions to improve your life in the year to come. When it comes to credit cards,
					the Nerds have some tips on how you can make 2017 better than 2016.
				</p><br/>
				<h4><b>Include credit card debt in your budget</b></h4>
				<p>
					According to the National Retail Federation, consumers planned to spend an average of $935.58 during the holidays. If you put more on your credit
					cards than you can pay off in full come January, resolve to wipe it out in 2017. Make a budget, and make debt payment one of the essential categories,
					along with food, housing, utilities, gas and other regular expenses. Figure out how much more than the minimum you can pay on your cards.<br/>
					The debt snowball method is helpful for staying motivated. Here’s how it works: Pay off the credit card with the lowest balance as soon as possible,
					while paying the minimum on your other cards. When card No. 1 is paid off, move on to the next lowest balance. Continue this process until your cards are paid off. 
					By tackling your debt systematically, you can celebrate small successes as you reach your debt-free goal.
				</p><br/>
				<b>» MORE:</b> Credit card payoff strategies<br/>
				<h4><b>Review your 2016 credit card statements</b></h4>
				<p>
					When you get your final monthly credit card statement of the year, go through it to see how you spent your money and which items are tax-deductible. Your credit card issuer may provide a downloadable annual statement, which usually breaks down spending by category so you can analyze it. <br/>
					Compare your spending to the budget you’ve made for 2017 to figure out where you can adjust to stay on track. Or find where you can cut back to put more money toward savings.
				</p><br/>
				<h4><b>Get the right credit card</b></h4>	
				<p>
					Now that you know where you spent your money last year, think about whether you’reusing the right credit card. Review the benefits and rewards on your 
					current credit cards. Are they relevant to your lifestyle?<br/>
					Maybe you’ve changed jobs and you’re driving more each week. In that case, consider a card with bonus rewards on gas. Maybe you’re planning a big 
					purchase and need a card with a long 0% APR period. Or maybe you want to pair two cards for different kinds of rewards, so you can use them to your best advantage.<br/>
					For example, the  Chase Freedom® gives you 5% cash back in bonus categories that you register for each quarter. The Chase Sapphire Preferred® Card gives you bonus points on dining and travel. 
					Book travel through Chase Ultimate Rewards, and points can be worth 25% more. Consider the cards you have and how you might add one to boost your rewards.<br/>
					Speaking of bonus categories, don’t forget to opt in to 2017’s first-quarter categories to get your 5% cash back if you have this type of card. For
					the Chase Freedom®, bonuses for Q1 2017 are gas stations and local commuter travel. On Discover cards, the Q1 2017 categories are gas, ground 
					transportation and wholesale clubs.
				</p><br/>
				<b>» MORE:</b> NerdWallet’s best balance transfer and 0% interest cards<br/>
				<h4><b>Negotiate better credit card terms</b></h4>
				<p>
					Often, people’s oldest credit card is also the card with the lowest credit limit and the highest APR, since they probably got it when they were fairly new to credit.<br/>
					The age of your credit accounts counts for 15% of your credit score, so you want to hang on to any old cards you have. But if it’s really not the best card for you, 
					call your credit card issuer and see whether you can raise your credit limit, lower your APR or even convert the account to a better card. If you’re a 
					good customer — you pay your bills on time and don’t go over your credit limit — your issuer will want to hold on to your business, which could work in your favor.<br/>
					If you can negotiate better terms, it’s a double win for your credit score: You keep your credit history intact while increasing your available credit.<br/>
					Here’s to 2017 and getting the new year started right.
				</p>
				<br/>
				<b>» MORE:</b> How to pay off debt
				<h4><b>If your resolution is ‘Get organized’</b></h4>
				<p>
					If your pocketbook is overflowing with credit cards you never use, consider New Year’s your opportunity to straighten up. Although it’s generally good 
					idea to keep dormant accounts open because your credit may benefit, it becomes a bad idea when there are annual fees involved. If a card you don’t use 
					charges an annual fee, either convert it to a no-fee account or close it.<br/>
					As long as you’re not about to apply for a mortgage or auto loan, the temporary hit to your credit from closing such cards likely won’t affect you much. 
					Just make sure you’ve logged onto your account and tied up all your loose ends first.
				</p><br/>
				<h4><b>How credit card tools can help</b></h4>
				<p>
					Calling it quits with your plastic usually means forfeiting rewards, such as points, miles or cash back. Before closing an account,
					check your rewards balance to make sure you’re not giving up a small fortune. If you have leftover rewards, redeem what you can.
					If that’s not possible, consider making a donation.<br/>
					Finally, move recurring payments to other cards or cancel them. Until you formally cancel an unwanted subscription, you’re still on the hook for payments.
					Start by sorting through your credit card purchase history to jog your memory about any forgotten subscriptions. 
					After moving or canceling your recurring charges, you’ll be able to close your account with confidence.
				</p><br/>

